create or replace function public._badge_norm_name(v text)
returns text
language sql
immutable
as $$
  select lower(trim(coalesce(v, '')))
$$;

create or replace function public._badge_normalize_drink_type(v text)
returns text
language sql
immutable
as $$
  select case
    when lower(regexp_replace(coalesce(v, ''), '[^a-z0-9]+', '', 'g')) in ('2bakken','tweebakken') then '2bakken'
    when lower(regexp_replace(coalesce(v, ''), '[^a-z0-9]+', '', 'g')) in ('literbier','liter_bier') then 'liter_bier'
    when lower(regexp_replace(coalesce(v, ''), '[^a-z0-9]+', '', 'g')) in ('wijnfles','fleswijn','wijn') then 'wijnfles'
    when lower(regexp_replace(coalesce(v, ''), '[^a-z0-9]+', '', 'g')) in ('ice','smirnoffice') then 'ice'
    when lower(regexp_replace(coalesce(v, ''), '[^a-z0-9]+', '', 'g')) in ('shot','shots') then 'shot'
    else 'bier'
  end
$$;

create or replace function public._badge_public_player_row_scoped(
  player_name_input text,
  site_scope_input text default 'friends'
)
returns jsonb
language plpgsql
stable
security definer
set search_path to 'public'
as $$
declare
  v_scope text := case when lower(trim(coalesce(site_scope_input, ''))) = 'family' then 'family' else 'friends' end;
  v_raw jsonb;
  v_players jsonb := '[]'::jsonb;
  v_row jsonb := null;
begin
  begin
    v_raw := public.get_all_site_players_public_scoped(site_scope_input => v_scope);
    if jsonb_typeof(v_raw) = 'object' and v_raw ? 'players' then
      v_players := coalesce(v_raw->'players', '[]'::jsonb);
    elsif jsonb_typeof(v_raw) = 'array' then
      v_players := v_raw;
    end if;
  exception when others then
    v_players := '[]'::jsonb;
  end;

  select value
    into v_row
    from jsonb_array_elements(v_players)
   where public._badge_norm_name(coalesce(
           value->>'player_name',
           value->>'display_name',
           value->>'chosen_username',
           value->>'public_display_name',
           value->>'nickname'
         )) = public._badge_norm_name(player_name_input)
      or public._badge_norm_name(coalesce(value->>'display_name', '')) = public._badge_norm_name(player_name_input)
      or public._badge_norm_name(coalesce(value->>'chosen_username', '')) = public._badge_norm_name(player_name_input)
      or public._badge_norm_name(coalesce(value->>'public_display_name', '')) = public._badge_norm_name(player_name_input)
      or public._badge_norm_name(coalesce(value->>'nickname', '')) = public._badge_norm_name(player_name_input)
   limit 1;

  return v_row;
end;
$$;

create or replace function public._badge_verified_drink_rows(player_name_input text)
returns table(
  canonical_type_key text,
  occurred_at timestamptz,
  total_units numeric,
  local_day date,
  local_hour integer
)
language sql
stable
as $$
  select
    public._badge_normalize_drink_type(coalesce(to_jsonb(de)->>'event_type_key', to_jsonb(de)->>'event_type_label', 'bier')) as canonical_type_key,
    coalesce(nullif(to_jsonb(de)->>'verified_at', '')::timestamptz, de.created_at) as occurred_at,
    coalesce(nullif(to_jsonb(de)->>'total_units', '')::numeric, coalesce(nullif(to_jsonb(de)->>'quantity', '')::numeric, 1)) as total_units,
    (coalesce(nullif(to_jsonb(de)->>'verified_at', '')::timestamptz, de.created_at) at time zone 'Europe/Amsterdam')::date as local_day,
    extract(hour from coalesce(nullif(to_jsonb(de)->>'verified_at', '')::timestamptz, de.created_at) at time zone 'Europe/Amsterdam')::int as local_hour
  from public.drink_events de
  left join public.players p on p.id = de.player_id
  where lower(coalesce(to_jsonb(de)->>'status', 'pending')) = 'verified'
    and public._badge_norm_name(coalesce(
      nullif(trim(coalesce(to_jsonb(de)->>'player_name', '')), ''),
      nullif(trim(coalesce(p.display_name, '')), ''),
      nullif(trim(coalesce(p.username, '')), '')
    )) = public._badge_norm_name(player_name_input)
$$;

create or replace function public._badge_verified_speed_rows(player_name_input text)
returns table(
  canonical_type_key text,
  occurred_at timestamptz,
  duration_seconds numeric,
  local_day date,
  local_hour integer
)
language sql
stable
as $$
  select
    public._badge_normalize_drink_type(coalesce(to_jsonb(dsa)->>'speed_type_key', to_jsonb(dsa)->>'event_type_key', 'bier')) as canonical_type_key,
    coalesce(nullif(to_jsonb(dsa)->>'verified_at', '')::timestamptz, dsa.created_at) as occurred_at,
    coalesce(nullif(to_jsonb(dsa)->>'duration_seconds', '')::numeric, 0) as duration_seconds,
    (coalesce(nullif(to_jsonb(dsa)->>'verified_at', '')::timestamptz, dsa.created_at) at time zone 'Europe/Amsterdam')::date as local_day,
    extract(hour from coalesce(nullif(to_jsonb(dsa)->>'verified_at', '')::timestamptz, dsa.created_at) at time zone 'Europe/Amsterdam')::int as local_hour
  from public.drink_speed_attempts dsa
  left join public.players p on p.id = dsa.player_id
  where lower(coalesce(to_jsonb(dsa)->>'status', 'pending')) = 'verified'
    and public._badge_norm_name(coalesce(
      nullif(trim(coalesce(to_jsonb(dsa)->>'player_name', '')), ''),
      nullif(trim(coalesce(p.display_name, '')), ''),
      nullif(trim(coalesce(p.username, '')), '')
    )) = public._badge_norm_name(player_name_input)
$$;

create or replace function public._badge_all_match_rows_scoped(
  player_name_input text,
  site_scope_input text default 'friends'
)
returns table(
  game_key text,
  match_ref text,
  finished_at timestamptz,
  is_win boolean,
  participants_count integer
)
language sql
stable
as $$
  with summary_rows as (
    select
      r.game_key,
      r.match_ref,
      r.finished_at,
      exists(select 1 from unnest(coalesce(r.winner_names, '{}'::text[])) w where public._badge_norm_name(w) = public._badge_norm_name(player_name_input)) as is_win,
      coalesce(array_length(r.participants, 1), 0) as participants_count,
      r.participants
    from public._gejast_summary_rows_scoped(false, site_scope_input) r
  ),
  summary_player as (
    select s.game_key, s.match_ref, s.finished_at, s.is_win, s.participants_count
    from summary_rows s
    where exists (select 1 from unnest(coalesce(s.participants, '{}'::text[])) p where public._badge_norm_name(p) = public._badge_norm_name(player_name_input))
  ),
  beerpong_player as (
    select
      'beerpong'::text as game_key,
      coalesce(nullif(trim(coalesce(m.client_match_id, '')), ''), m.id::text) as match_ref,
      coalesce(m.finished_at, m.created_at) as finished_at,
      case
        when lower(coalesce(m.winner_team, '')) = 'a' and exists (select 1 from public.players p where p.id = any(coalesce(m.team_a_player_ids, '{}'::bigint[])) and public._badge_norm_name(p.display_name) = public._badge_norm_name(player_name_input)) then true
        when lower(coalesce(m.winner_team, '')) = 'b' and exists (select 1 from public.players p where p.id = any(coalesce(m.team_b_player_ids, '{}'::bigint[])) and public._badge_norm_name(p.display_name) = public._badge_norm_name(player_name_input)) then true
        else false
      end as is_win,
      coalesce(array_length(coalesce(m.team_a_player_ids, '{}'::bigint[]), 1), 0) + coalesce(array_length(coalesce(m.team_b_player_ids, '{}'::bigint[]), 1), 0) as participants_count
    from public.beerpong_matches m
    where coalesce(m.deleted_at is null, true)
      and coalesce(lower(m.match_status), 'finished') = 'finished'
      and site_scope_input <> 'family'
      and (
        exists (select 1 from public.players p where p.id = any(coalesce(m.team_a_player_ids, '{}'::bigint[])) and public._badge_norm_name(p.display_name) = public._badge_norm_name(player_name_input))
        or
        exists (select 1 from public.players p where p.id = any(coalesce(m.team_b_player_ids, '{}'::bigint[])) and public._badge_norm_name(p.display_name) = public._badge_norm_name(player_name_input))
      )
  )
  select * from summary_player
  union all
  select * from beerpong_player
$$;

create or replace function public._ballroom_track_king_history_v352()
returns trigger
language plpgsql
as $$
begin
  if tg_op = 'INSERT' then
    if nullif(trim(coalesce(new.king_name, '')), '') is not null then
      if not exists (
        select 1 from public.ballroom_safe_king_history h
        where public._badge_norm_name(h.player_name) = public._badge_norm_name(new.king_name)
          and h.released_at is null
      ) then
        insert into public.ballroom_safe_king_history(player_name, claimed_at, source_event)
        values (new.king_name, coalesce(new.claimed_at, new.updated_at, now()), 'state_insert');
      end if;
    end if;
    return new;
  end if;

  if public._badge_norm_name(old.king_name) is distinct from public._badge_norm_name(new.king_name) then
    if nullif(trim(coalesce(old.king_name, '')), '') is not null then
      update public.ballroom_safe_king_history
         set released_at = coalesce(new.updated_at, now()),
             release_reason = case when nullif(trim(coalesce(new.king_name, '')), '') is null then 'abdicated' else 'replaced' end
       where public._badge_norm_name(player_name) = public._badge_norm_name(old.king_name)
         and released_at is null;
    end if;
    if nullif(trim(coalesce(new.king_name, '')), '') is not null then
      insert into public.ballroom_safe_king_history(player_name, claimed_at, source_event)
      values (new.king_name, coalesce(new.claimed_at, new.updated_at, now()), 'state_update');
    end if;
  end if;
  return new;
end;
$$;

