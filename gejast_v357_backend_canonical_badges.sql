  v_total_matches integer := 0;
  v_total_wins integer := 0;
  v_total_site_actions integer := 0;
  v_klaverjas_matches integer := 0;
  v_boerenbridge_matches integer := 0;
  v_beerpong_matches integer := 0;
  v_klaverjas_wins integer := 0;
  v_boerenbridge_wins integer := 0;
  v_beerpong_wins integer := 0;
  v_best_game_win_pct numeric := 0;
  v_verified_drink_actions integer := 0;
  v_total_drink_units numeric := 0;
  v_verified_speed_attempts integer := 0;
