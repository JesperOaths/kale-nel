      with ordered as (
        select finished_at, match_ref, is_win,
               sum(case when not is_win then 1 else 0 end) over(order by finished_at, match_ref) as grp
        from match_rows
      )
      select max(streak)::int
      from (
        select grp, count(*) filter (where is_win) as streak
        from ordered
        group by grp
      ) s
    ), 0),
    coalesce((
      select count(distinct (finished_at at time zone 'Europe/Amsterdam')::date)
      from match_rows
      where is_win
