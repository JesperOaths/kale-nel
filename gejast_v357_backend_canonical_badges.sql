create or replace function public.ballroom_resolve_request_safe(
  session_token text default null,
  session_token_input text default null,
  request_id_input bigint default null,
  approve_input boolean default false
)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  viewer_name text := public._ballroom_safe_require_name(session_token, session_token_input);
  king_name text;
  req_name text;
begin
  select s.king_name into king_name from public.ballroom_safe_state s where s.id = 1;
  if lower(coalesce(king_name, '')) <> lower(coalesce(viewer_name, '')) then
    raise exception 'Alleen de koning mag verzoeken beoordelen.';
  end if;
  if request_id_input is null then
    raise exception 'Er ontbreekt een verzoek-id.';
  end if;
  select r.player_name into req_name
  from public.ballroom_safe_requests r
  where r.id = request_id_input
    and r.status = 'pending';
  if req_name is null then
    raise exception 'Dit entreeverzoek is niet meer actief.';
  end if;

  update public.ballroom_safe_requests
     set status = case when approve_input then 'approved' else 'rejected' end,
         resolved_at = now(),
         resolved_by_name = viewer_name
   where id = request_id_input
     and status = 'pending';

  if approve_input then
    insert into public.ballroom_safe_members(player_name, approved_at, updated_at)
    values (req_name, now(), now())
    on conflict (player_name) do update set updated_at = now();
  end if;

  return public.get_ballroom_public_state_safe(session_token, session_token_input);
end;
$$;

insert into public.ballroom_safe_king_history(player_name, claimed_at, source_event)
select s.king_name, coalesce(s.claimed_at, s.updated_at, now()), 'backfill'
from public.ballroom_safe_state s
where s.id = 1
  and nullif(trim(coalesce(s.king_name, '')), '') is not null
  and not exists (
    select 1
    from public.ballroom_safe_king_history h
    where public._badge_norm_name(h.player_name) = public._badge_norm_name(s.king_name)
      and h.released_at is null
  );

insert into public.ballroom_safe_request_history(request_id, player_name, requested_at, resolved_status, resolved_by_name, resolved_at)
select r.id, r.player_name, r.requested_at, r.status, r.resolved_by_name, coalesce(r.resolved_at, now())
from public.ballroom_safe_requests r
where r.status in ('approved', 'rejected')
  and not exists (
    select 1 from public.ballroom_safe_request_history h where h.request_id = r.id
  );

