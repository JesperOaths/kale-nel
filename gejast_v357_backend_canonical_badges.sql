  by_game as (
    select game_key, count(*)::int as matches, count(*) filter (where is_win)::int as wins
    from match_rows
    group by game_key
  )
  select
    coalesce((select sum(matches) from by_game), 0),
    coalesce((select sum(wins) from by_game), 0),
    coalesce((select matches from by_game where game_key = 'klaverjas'), 0),
    coalesce((select matches from by_game where game_key = 'boerenbridge'), 0),
    coalesce((select matches from by_game where game_key = 'beerpong'), 0),
