create or replace function public.get_paardenrace_stats_public(session_token text default null, session_token_input text default null)
returns jsonb language plpgsql security definer as $$
declare
  v_overview jsonb;
  v_recent jsonb;
begin
  select jsonb_build_object(
    'matches_played', coalesce((select count(*) from public.paardenrace_match_history),0),
    'total_bakken', coalesce((select sum(amount_bakken) from public.paardenrace_obligations),0),
    'last_winner_suit', (select h.winner_suit from public.paardenrace_match_history h order by h.finished_at desc limit 1)
  ) into v_overview;

  select coalesce(jsonb_agg(jsonb_build_object(
    'room_code', h.room_code,
    'winner_suit', h.winner_suit,
    'player_names', coalesce(h.summary_payload->'per_player', '[]'::jsonb),
    'finished_at', h.finished_at
  ) order by h.finished_at desc), '[]'::jsonb)
  into v_recent
  from (
    select *
    from public.paardenrace_match_history
    order by finished_at desc
    limit 20
  ) h;

  return jsonb_build_object('overview', v_overview, 'recent_matches', v_recent);
end;
$$;

