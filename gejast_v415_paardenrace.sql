create or replace function public.update_paardenrace_room_choice_safe(session_token text default null, session_token_input text default null, room_code_input text default null, selected_suit_input text default null, wager_bakken_input integer default null, ready_input boolean default false)
returns jsonb language plpgsql security definer as $$
declare
  v_name text := public._paardenrace_require_name(session_token, session_token_input);
  v_room public.paardenrace_rooms%rowtype;
  v_suit text := lower(trim(coalesce(selected_suit_input,'')));
begin
  select * into v_room
  from public.paardenrace_rooms
  where room_code = upper(trim(coalesce(room_code_input,'')));

  if v_room.id is null then
    raise exception 'Room niet gevonden.';
  end if;

  if v_suit not in ('hearts','diamonds','clubs','spades') then
    v_suit := null;
  end if;

  update public.paardenrace_room_players
  set selected_suit = v_suit,
      wager_bakken = wager_bakken_input,
      is_ready = case
        when ready_input is true and v_suit is not null and wager_bakken_input is not null and wager_bakken_input > 0 then true
        else false end,
      updated_at = now()
  where room_id = v_room.id
    and lower(player_name) = lower(v_name);

  update public.paardenrace_rooms set updated_at = now() where id = v_room.id;

  return public._paardenrace_build_room_state(v_room.room_code, session_token, session_token_input);
end;
$$;

create or replace function public.start_paardenrace_countdown_safe(session_token text default null, session_token_input text default null, room_code_input text default null)
returns jsonb language plpgsql security definer as $$
declare
  v_name text := public._paardenrace_require_name(session_token, session_token_input);
  v_room public.paardenrace_rooms%rowtype;
begin
  select * into v_room
  from public.paardenrace_rooms
  where room_code = upper(trim(coalesce(room_code_input,'')));

  if v_room.id is null then
    raise exception 'Room niet gevonden.';
  end if;
  if lower(v_room.host_name) <> lower(v_name) then
    raise exception 'Alleen de host mag starten.';
  end if;
  if not public._paardenrace_all_ready(v_room.id) then
    raise exception 'Nog niet iedereen is ready.';
  end if;

  update public.paardenrace_rooms
  set stage = 'countdown',
      countdown_ends_at = now() + interval '5 seconds',
      updated_at = now()
  where id = v_room.id;

  return public._paardenrace_build_room_state(v_room.room_code, session_token, session_token_input);
end;
$$;

create or replace function public.tick_paardenrace_room_safe(session_token text default null, session_token_input text default null, room_code_input text default null)
returns jsonb language plpgsql security definer as $$
declare
  v_room public.paardenrace_rooms%rowtype;
  v_decks jsonb;
  v_match_ref uuid;
begin
  select * into v_room
  from public.paardenrace_rooms
  where room_code = upper(trim(coalesce(room_code_input,'')));

  if v_room.id is null then
    raise exception 'Room niet gevonden.';
  end if;

  if v_room.stage = 'countdown' and v_room.countdown_ends_at is not null and v_room.countdown_ends_at <= now() then
    v_decks := public._paardenrace_make_decks();
    v_match_ref := gen_random_uuid();

    update public.paardenrace_rooms
    set stage = 'race',
        countdown_ends_at = null,
        active_match = jsonb_build_object(
          'match_ref', v_match_ref,
          'gate_cards', v_decks->'gate_cards',
          'draw_deck', v_decks->'draw_deck',
          'draw_index', 0,
          'revealed_draw_cards', '[]'::jsonb,
          'horse_positions', jsonb_build_object('hearts',0,'diamonds',0,'clubs',0,'spades',0),
          'revealed_gates', '[]'::jsonb,
          'gate_events', '[]'::jsonb,
          'winner_submitted', '{}'::jsonb,
          'nominations', '[]'::jsonb,
          'first_finish_suit', null,
          'first_claimed_finish_suit', null,
          'winner_suit', null,
          'last_draw_card', null
        ),
        updated_at = now(),
        finished_at = null
    where id = v_room.id;

    insert into public.paardenrace_obligations(room_id, match_ref, player_id, player_name, amount_bakken, source_kind, metadata)
    select v_room.id, v_match_ref, rp.player_id, rp.player_name, rp.wager_bakken, 'wager',
           jsonb_build_object('source_game','paardenrace','source_kind','wager','selected_suit',rp.selected_suit)
    from public.paardenrace_room_players rp
    where rp.room_id = v_room.id
      and coalesce(rp.wager_bakken,0) > 0;
  end if;

  return public._paardenrace_build_room_state(v_room.room_code, session_token, session_token_input);
end;
$$;

create or replace function public.draw_paardenrace_card_safe(session_token text default null, session_token_input text default null, room_code_input text default null)
returns jsonb language plpgsql security definer as $$
declare
  v_name text := public._paardenrace_require_name(session_token, session_token_input);
  v_room public.paardenrace_rooms%rowtype;
  m jsonb;
  draw_idx integer;
  draw_deck jsonb;
  gate_cards jsonb;
  positions jsonb;
  revealed jsonb;
  gate_events jsonb;
  card text;
  suit text;
  gate_no integer;
  gate_card text;
  gate_suit text;
  all_ready_gate boolean;
  winner_suit text;
begin
  select * into v_room
  from public.paardenrace_rooms
  where room_code = upper(trim(coalesce(room_code_input,'')));

  if v_room.id is null then
    raise exception 'Room niet gevonden.';
  end if;
  if lower(v_room.host_name) <> lower(v_name) then
    raise exception 'Alleen de host mag trekken.';
  end if;
  if v_room.stage <> 'race' then
    raise exception 'De race is niet actief.';
  end if;

  m := v_room.active_match;
  draw_idx := coalesce((m->>'draw_index')::integer, 0);
  draw_deck := coalesce(m->'draw_deck', '[]'::jsonb);
  gate_cards := coalesce(m->'gate_cards', '[]'::jsonb);
  positions := coalesce(m->'horse_positions', jsonb_build_object('hearts',0,'diamonds',0,'clubs',0,'spades',0));
  revealed := coalesce(m->'revealed_gates', '[]'::jsonb);
  gate_events := '[]'::jsonb;

  card := draw_deck->>draw_idx;
  if card is null then
    raise exception 'De trekstapel is leeg.';
  end if;

  suit := public._paardenrace_suit_from_card(card);
  positions := jsonb_set(positions, array[suit], to_jsonb(least(11, coalesce((positions->>suit)::integer,0) + 1)), true);

  if coalesce(m->>'first_finish_suit','') = '' and coalesce((positions->>suit)::integer,0) >= 11 then
    m := jsonb_set(m, '{first_finish_suit}', to_jsonb(suit), true);
  end if;

  for gate_no in 1..10 loop
    if exists(select 1 from jsonb_array_elements_text(revealed) v where v.value::integer = gate_no) then
      continue;
    end if;

    all_ready_gate := coalesce((positions->>'hearts')::integer,0) >= gate_no
      and coalesce((positions->>'diamonds')::integer,0) >= gate_no
      and coalesce((positions->>'clubs')::integer,0) >= gate_no
      and coalesce((positions->>'spades')::integer,0) >= gate_no;

    if all_ready_gate then
      gate_card := gate_cards->>(gate_no - 1);
      gate_suit := public._paardenrace_suit_from_card(gate_card);
      positions := jsonb_set(positions, array[gate_suit], to_jsonb(greatest(0, coalesce((positions->>gate_suit)::integer,0) - 1)), true);
      revealed := revealed || to_jsonb(gate_no);
      gate_events := gate_events || jsonb_build_array(jsonb_build_object('gate_no', gate_no, 'card_code', gate_card, 'suit', gate_suit));
    end if;
  end loop;

  if coalesce(m->>'winner_suit','') = '' then
    select min(suit_key) into winner_suit
    from (values ('hearts'),('diamonds'),('clubs'),('spades')) s(suit_key)
    where coalesce((positions->>suit_key)::integer,0) >= 11
      and exists(
        select 1
        from public.paardenrace_room_players rp
        where rp.room_id = v_room.id
          and lower(coalesce(rp.selected_suit,'')) = lower(suit_key)
      );

    if winner_suit is not null then
      m := jsonb_set(m, '{winner_suit}', to_jsonb(winner_suit), true);
      m := jsonb_set(m, '{first_claimed_finish_suit}', to_jsonb(winner_suit), true);
      update public.paardenrace_rooms set stage = 'nominations', updated_at = now() where id = v_room.id;
    end if;
  end if;

  m := jsonb_set(m, '{draw_index}', to_jsonb(draw_idx + 1), true);
  m := jsonb_set(m, '{last_draw_card}', to_jsonb(card), true);
  m := jsonb_set(m, '{revealed_draw_cards}', coalesce(m->'revealed_draw_cards','[]'::jsonb) || to_jsonb(card), true);
  m := jsonb_set(m, '{horse_positions}', positions, true);
  m := jsonb_set(m, '{revealed_gates}', revealed, true);
  m := jsonb_set(m, '{gate_events}', gate_events, true);

  update public.paardenrace_rooms
  set active_match = m,
      updated_at = now()
  where id = v_room.id;

  return public._paardenrace_build_room_state(v_room.room_code, session_token, session_token_input);
end;
$$;

create or replace function public.submit_paardenrace_nominations_safe(session_token text default null, session_token_input text default null, room_code_input text default null, allocations_input jsonb default '[]'::jsonb)
returns jsonb language plpgsql security definer as $$
declare
  v_name text := public._paardenrace_require_name(session_token, session_token_input);
  v_player_id bigint := public._paardenrace_player_id(session_token, session_token_input);
  v_room public.paardenrace_rooms%rowtype;
  m jsonb;
  v_budget integer;
  v_total integer := 0;
  row jsonb;
  tgt text;
  b integer;
  v_match_ref uuid;
begin
  select * into v_room
  from public.paardenrace_rooms
  where room_code = upper(trim(coalesce(room_code_input,'')));

  if v_room.id is null then
    raise exception 'Room niet gevonden.';
  end if;
  if v_room.stage <> 'nominations' then
    raise exception 'Nominatiefase is niet actief.';
  end if;

  m := coalesce(v_room.active_match, '{}'::jsonb);
  v_match_ref := nullif(coalesce(m->>'match_ref',''),'')::uuid;

  select rp.wager_bakken * 2 into v_budget
  from public.paardenrace_room_players rp
  where rp.room_id = v_room.id
    and lower(rp.player_name) = lower(v_name)
    and lower(coalesce(rp.selected_suit,'')) = lower(coalesce(m->>'winner_suit',''))
  limit 1;

  if v_budget is null then
    raise exception 'Alleen winnaars mogen nomineren.';
  end if;
  if coalesce((m->'winner_submitted'->>lower(v_name))::boolean, false) then
    raise exception 'Jouw nominaties zijn al opgeslagen.';
  end if;

  for row in select value from jsonb_array_elements(coalesce(allocations_input,'[]'::jsonb)) loop
    tgt := trim(coalesce(row->>'target_player_name',''));
    b := coalesce((row->>'bakken')::integer, 0);
    if b <= 0 then continue; end if;
    if lower(tgt) = lower(v_name) then
      raise exception 'Je mag jezelf niet nomineren.';
    end if;
    if not exists(select 1 from public.paardenrace_room_players rp where rp.room_id = v_room.id and lower(rp.player_name) = lower(tgt)) then
      raise exception 'Onbekende speler in nominaties: %', tgt;
    end if;
    v_total := v_total + b;
  end loop;

  if v_total <> v_budget then
    raise exception 'Je moet exact % Bakken verdelen.', v_budget;
  end if;

  for row in select value from jsonb_array_elements(coalesce(allocations_input,'[]'::jsonb)) loop
    tgt := trim(coalesce(row->>'target_player_name',''));
    b := coalesce((row->>'bakken')::integer, 0);
    if b <= 0 then continue; end if;

    insert into public.paardenrace_obligations(room_id, match_ref, player_id, player_name, amount_bakken, source_kind, metadata)
    values (
      v_room.id,
      v_match_ref,
      null,
      tgt,
      b,
      'winner_nomination',
      jsonb_build_object(
        'source_game','paardenrace',
        'source_kind','winner_nomination',
        'winner_player_id',v_player_id,
        'winner_player_name',v_name,
        'target_player_name',tgt
      )
    );
  end loop;

  m := jsonb_set(m, '{nominations}', coalesce(m->'nominations','[]'::jsonb) || allocations_input, true);
  m := jsonb_set(m, array['winner_submitted', lower(v_name)], 'true'::jsonb, true);

  update public.paardenrace_rooms
  set active_match = m,
      updated_at = now()
  where id = v_room.id;

  if not exists(
    select 1
    from public.paardenrace_room_players rp
    where rp.room_id = v_room.id
      and lower(coalesce(rp.selected_suit,'')) = lower(coalesce(m->>'winner_suit',''))
      and not coalesce((m->'winner_submitted'->>lower(rp.player_name))::boolean, false)
  ) then
    insert into public.paardenrace_match_history(room_id, room_code, match_ref, winner_suit, summary_payload, finished_at)
    values (
      v_room.id,
      v_room.room_code,
      v_match_ref,
      m->>'winner_suit',
      public._paardenrace_result_summary(v_room.id, m),
      now()
    );

    update public.paardenrace_rooms
    set stage = 'finished',
        active_match = m,
        finished_at = now(),
        updated_at = now()
    where id = v_room.id;
  end if;

  return public._paardenrace_build_room_state(v_room.room_code, session_token, session_token_input);
end;
$$;

create or replace function public.reset_paardenrace_room_safe(session_token text default null, session_token_input text default null, room_code_input text default null)
returns jsonb language plpgsql security definer as $$
declare
  v_name text := public._paardenrace_require_name(session_token, session_token_input);
  v_room public.paardenrace_rooms%rowtype;
begin
  select * into v_room
  from public.paardenrace_rooms
  where room_code = upper(trim(coalesce(room_code_input,'')));

  if v_room.id is null then
    raise exception 'Room niet gevonden.';
  end if;
  if lower(v_room.host_name) <> lower(v_name) then
    raise exception 'Alleen de host mag resetten.';
  end if;

  update public.paardenrace_room_players
  set selected_suit = null,
      wager_bakken = null,
      is_ready = false,
      updated_at = now()
  where room_id = v_room.id;

  update public.paardenrace_rooms
  set stage = 'lobby',
      countdown_ends_at = null,
      active_match = '{}'::jsonb,
      updated_at = now()
  where id = v_room.id;

  return public._paardenrace_build_room_state(v_room.room_code, session_token, session_token_input);
end;
$$;

