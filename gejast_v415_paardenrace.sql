-- Matches the reuploaded paardenrace frontend owner path:
-- - paardenrace.html
-- - paardenrace_live.html
-- - paardenrace_stats.html
-- - gejast-paardenrace.js
--
-- Core model:
-- - room/lobby state stays server-authoritative
-- - active match state lives in public.paardenrace_rooms.active_match jsonb
-- - completed match snapshots are copied into public.paardenrace_match_history
-- - obligations are stored in public.paardenrace_obligations

create extension if not exists pgcrypto;

create table if not exists public.paardenrace_rooms (
  id bigint generated by default as identity primary key,
  room_code text not null unique,
  host_player_id bigint,
  host_name text not null,
  stage text not null default 'lobby',
  countdown_ends_at timestamptz,
  active_match jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  finished_at timestamptz
);

create table if not exists public.paardenrace_room_players (
  id bigint generated by default as identity primary key,
  room_id bigint not null references public.paardenrace_rooms(id) on delete cascade,
  player_id bigint,
  player_name text not null,
  player_name_norm text generated always as (lower(player_name)) stored,
  selected_suit text,
  wager_bakken integer,
  is_ready boolean not null default false,
  joined_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique(room_id, player_name_norm)
);

create table if not exists public.paardenrace_obligations (
  id bigint generated by default as identity primary key,
  room_id bigint not null references public.paardenrace_rooms(id) on delete cascade,
  match_ref uuid,
  player_id bigint,
  player_name text not null,
  amount_bakken integer not null,
  source_kind text not null,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create table if not exists public.paardenrace_match_history (
  id bigint generated by default as identity primary key,
  room_id bigint references public.paardenrace_rooms(id) on delete set null,
  room_code text,
  match_ref uuid not null,
  winner_suit text,
  summary_payload jsonb not null default '{}'::jsonb,
  finished_at timestamptz not null default now()
);

create or replace function public._paardenrace_safe_token(session_token text default null, session_token_input text default null)
returns text language sql immutable as $$
  select nullif(trim(coalesce(session_token, session_token_input, '')), '')
$$;

