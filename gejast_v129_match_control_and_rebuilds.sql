create or replace function public.player_update_match_action(
  game_type_input text,
  session_token text,
  match_ref text,
  match_payload jsonb default '{}'::jsonb,
  delete_match boolean default false,
  reason_input text default null
) returns jsonb language plpgsql security definer set search_path to 'public' as $fn$
declare
  v_game text := lower(trim(coalesce(game_type_input,'')));
  v_player public.players%rowtype;
  before_payload jsonb;
  v_name text;
  participant jsonb;
  resolved_player_id bigint;
  v_allowed boolean := false;
begin
  select * into v_player from public._gejast_player_from_session(session_token);
  v_name := v_player.display_name;

  if v_game = 'beerpong' then
    select (created_by_player_id = v_player.id and now() <= created_at + interval '5 minutes') into v_allowed from public.beerpong_matches where client_match_id = match_ref;
    if not coalesce(v_allowed,false) then raise exception 'Bewerkenstijd verlopen.'; end if;
    select payload into before_payload from public.beerpong_matches where client_match_id = match_ref;
    if delete_match then
      update public.beerpong_matches set deleted_at = now(), updated_at = now() where client_match_id = match_ref;
    else
      update public.beerpong_matches
         set winner_team = coalesce(nullif(match_payload->>'winner_team',''), winner_team),
             team_a_player_names = coalesce((select array_agg(value::text) from jsonb_array_elements_text(coalesce(match_payload->'team_a_player_names', to_jsonb(team_a_player_names)))), team_a_player_names),
             team_b_player_names = coalesce((select array_agg(value::text) from jsonb_array_elements_text(coalesce(match_payload->'team_b_player_names', to_jsonb(team_b_player_names)))), team_b_player_names),
             team_a_cups_left = coalesce(nullif(match_payload->>'team_a_cups_left','')::integer, team_a_cups_left),
             team_b_cups_left = coalesce(nullif(match_payload->>'team_b_cups_left','')::integer, team_b_cups_left),
             finished_at = coalesce(nullif(match_payload->>'finished_at','')::timestamptz, finished_at),
             payload = match_payload,
             deleted_at = null,
             updated_at = now()
       where client_match_id = match_ref;
    end if;
  elsif v_game = 'boerenbridge' then
    select (created_by_player_id = v_player.id and now() <= created_at + interval '5 minutes') into v_allowed from public.boerenbridge_matches where client_match_id = match_ref;
    if not coalesce(v_allowed,false) then raise exception 'Bewerkenstijd verlopen.'; end if;
    select payload into before_payload from public.boerenbridge_matches where client_match_id = match_ref;
    if delete_match then
      update public.boerenbridge_matches set deleted_at = now(), updated_at = now() where client_match_id = match_ref;
    else
      update public.boerenbridge_matches
         set payload = match_payload,
             finished_at = coalesce(nullif(match_payload->>'finished_at','')::timestamptz, finished_at),
             match_status = case when nullif(match_payload->>'finished_at','') is not null then 'finished' else coalesce(match_status,'draft') end,
             deleted_at = null,
             updated_at = now()
       where client_match_id = match_ref;
    end if;
  elsif v_game = 'klaverjas' then
    select (created_by_player_id = v_player.id and now() <= created_at + interval '5 minutes') into v_allowed from public.jas_games where id = match_ref::bigint;
    if not coalesce(v_allowed,false) then raise exception 'Bewerkenstijd verlopen.'; end if;
    select payload into before_payload from public.jas_games where id = match_ref::bigint;
    if delete_match then
      update public.jas_games set deleted_at = now(), updated_at = now() where id = match_ref::bigint;
    else
      update public.jas_games
         set title = coalesce(nullif(trim(coalesce(match_payload->>'title','')), ''), title),
             played_at = coalesce(nullif(match_payload->>'played_at','')::date, played_at),
             payload = match_payload,
             deleted_at = null,
             updated_at = now()
       where id = match_ref::bigint;
      delete from public.jas_game_entries where game_id = match_ref::bigint;
      for participant in select * from jsonb_array_elements(coalesce(match_payload->'participants','[]'::jsonb)) loop
        select p.id into resolved_player_id from public.players p where lower(p.display_name) = lower(trim(participant ->> 'name')) limit 1;
        insert into public.jas_game_entries(game_id, player_id, display_name, seat_no, team_no, total_points, is_winner)
        values (match_ref::bigint, resolved_player_id, participant->>'name', (participant->>'seat_no')::integer, (participant->>'team_no')::integer, coalesce((participant->>'total_points')::integer,0), coalesce((participant->>'is_winner')::boolean,false));
      end loop;
    end if;
  else
    raise exception 'Onbekend speltype';
  end if;

  insert into public.match_change_log(game_type, match_ref, change_type, changed_by_role, changed_by_name, reason, before_payload, after_payload)
  values (v_game, match_ref, case when delete_match then 'delete' else 'update' end, 'player', v_name, reason_input, before_payload, match_payload);
  perform public._enqueue_rating_rebuild(v_game, case when delete_match then 'player_delete' else 'player_update' end, match_ref, 'player', v_name);
  perform public.process_game_rating_rebuild_queue(10);
  return jsonb_build_object('ok', true);
end;
$fn$;

