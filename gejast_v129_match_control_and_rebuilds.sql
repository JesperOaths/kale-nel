declare
  v_game text := lower(trim(coalesce(game_type_input,'')));
  v_limit integer := greatest(coalesce(limit_count,30),1);
  v_player public.players%rowtype;
  v_admin jsonb;
  result jsonb;
begin
  begin
    if nullif(trim(coalesce(session_token,'')), '') is not null then
      select * into v_player from public._gejast_player_from_session(session_token);
    end if;
  exception when others then null; end;

  begin
    if nullif(trim(coalesce(admin_session_token,'')), '') is not null then
      v_admin := to_jsonb(public.admin_check_session(admin_session_token));
    end if;
  exception when others then v_admin := null; end;

  if v_game = 'beerpong' then
    select coalesce(jsonb_agg(row_json order by created_at desc), '[]'::jsonb) into result
    from (
      select m.created_at,
        jsonb_build_object(
          'match_ref', m.client_match_id,
          'title', coalesce(array_to_string(m.team_a_player_names, ' + ') || ' vs ' || array_to_string(m.team_b_player_names, ' + '), 'Beerpong'),
          'created_at', m.created_at,
          'updated_at', m.updated_at,
          'finished_at', m.finished_at,
          'editable_until', m.created_at + interval '5 minutes',
          'can_edit', (v_admin is not null) or (v_player.id is not null and m.created_by_player_id = v_player.id and now() <= m.created_at + interval '5 minutes'),
          'deleted_at', m.deleted_at,
          'summary', jsonb_build_object('winner_team', m.winner_team, 'team_a', m.team_a_player_names, 'team_b', m.team_b_player_names)
        ) as row_json
      from public.beerpong_matches m
      where ((v_admin is not null) or (v_player.id is not null and m.created_by_player_id = v_player.id))
      order by m.created_at desc
      limit v_limit
    ) s;
  elsif v_game = 'boerenbridge' then
    select coalesce(jsonb_agg(row_json order by created_at desc), '[]'::jsonb) into result
    from (
      select m.created_at,
        jsonb_build_object(
          'match_ref', m.client_match_id,
          'title', coalesce(m.match_name, 'Boerenbridge'),
          'created_at', m.created_at,
          'updated_at', m.updated_at,
          'finished_at', m.finished_at,
          'editable_until', m.created_at + interval '5 minutes',
          'can_edit', (v_admin is not null) or (v_player.id is not null and m.created_by_player_id = v_player.id and now() <= m.created_at + interval '5 minutes'),
          'deleted_at', m.deleted_at,
          'summary', jsonb_build_object('participants', coalesce(m.payload->'participants', m.payload->'players', '[]'::jsonb), 'winners', coalesce(m.payload#>'{match_summary,winner_names}', '[]'::jsonb))
        ) as row_json
      from public.boerenbridge_matches m
      where ((v_admin is not null) or (v_player.id is not null and m.created_by_player_id = v_player.id))
      order by m.created_at desc
      limit v_limit
    ) s;
  elsif v_game = 'klaverjas' then
    select coalesce(jsonb_agg(row_json order by created_at desc), '[]'::jsonb) into result
    from (
      select j.created_at,
        jsonb_build_object(
          'match_ref', j.id::text,
          'title', j.title,
          'created_at', j.created_at,
          'updated_at', j.updated_at,
          'finished_at', j.created_at,
          'editable_until', j.created_at + interval '5 minutes',
          'can_edit', (v_admin is not null) or (v_player.id is not null and j.created_by_player_id = v_player.id and now() <= j.created_at + interval '5 minutes'),
          'deleted_at', j.deleted_at,
          'summary', jsonb_build_object('players', coalesce((select jsonb_agg(e.display_name order by e.seat_no) from public.jas_game_entries e where e.game_id = j.id), '[]'::jsonb))
        ) as row_json
      from public.jas_games j
      where ((v_admin is not null) or (v_player.id is not null and j.created_by_player_id = v_player.id))
      order by j.created_at desc
      limit v_limit
    ) s;
  else
    raise exception 'Onbekend speltype';
  end if;

  return jsonb_build_object('ok', true, 'matches', result, 'is_admin', v_admin is not null, 'player_name', v_player.display_name);
end;
