create or replace function public.rebuild_beerpong_ratings()
returns void language plpgsql security definer set search_path to 'public' as $fn$
declare
  m record;
  a_players text[];
  b_players text[];
  team_a_avg numeric;
  team_b_avg numeric;
  expected_a numeric;
  actual_a numeric;
  k_factor numeric := 28.0;
  margin_factor numeric;
  delta_total numeric;
  p text;
  before_rating numeric;
  after_rating numeric;
  ts timestamptz;
  winner_a boolean;
begin
  truncate table public.beerpong_player_rating_history restart identity;
  delete from public.beerpong_player_ratings;

  for m in
    select *
    from public.beerpong_matches
    where coalesce(deleted_at is null, true)
    order by coalesce(finished_at, created_at), id
  loop
    a_players := coalesce(m.team_a_player_names, '{}'::text[]);
    b_players := coalesce(m.team_b_player_names, '{}'::text[]);
    if coalesce(array_length(a_players,1),0) = 0 or coalesce(array_length(b_players,1),0) = 0 then
      continue;
    end if;
    ts := coalesce(m.finished_at, m.created_at, now());
    winner_a := lower(coalesce(m.winner_team,'')) in ('a','team_a');

    foreach p in array a_players loop
      insert into public.beerpong_player_ratings(player_name) values (p) on conflict (player_name) do nothing;
    end loop;
    foreach p in array b_players loop
      insert into public.beerpong_player_ratings(player_name) values (p) on conflict (player_name) do nothing;
    end loop;

    select avg(elo_rating) into team_a_avg from public.beerpong_player_ratings where player_name = any(a_players);
    select avg(elo_rating) into team_b_avg from public.beerpong_player_ratings where player_name = any(b_players);
    expected_a := public._elo_expected(team_a_avg, team_b_avg);
    actual_a := case when winner_a then 1 else 0 end;
    margin_factor := least(1.25, greatest(1.0, 1.0 + abs(coalesce(m.team_a_cups_left,0) - coalesce(m.team_b_cups_left,0))::numeric / 20.0));
    delta_total := k_factor * margin_factor * (actual_a - expected_a);

    foreach p in array a_players loop
      select elo_rating into before_rating from public.beerpong_player_ratings where player_name = p;
      update public.beerpong_player_ratings
         set elo_rating = elo_rating + (delta_total / greatest(coalesce(array_length(a_players,1),1),1)),
             games_played = games_played + 1,
             wins = wins + case when winner_a then 1 else 0 end,
             losses = losses + case when not winner_a then 1 else 0 end,
             last_match_at = ts,
             updated_at = now()
       where player_name = p;
      select elo_rating into after_rating from public.beerpong_player_ratings where player_name = p;
      insert into public.beerpong_player_rating_history(match_id, player_name, rating_before, rating_after, created_at)
      values (m.id, p, coalesce(before_rating,1000), coalesce(after_rating,1000), ts);
    end loop;

    foreach p in array b_players loop
      select elo_rating into before_rating from public.beerpong_player_ratings where player_name = p;
      update public.beerpong_player_ratings
         set elo_rating = elo_rating - (delta_total / greatest(coalesce(array_length(b_players,1),1),1)),
             games_played = games_played + 1,
             wins = wins + case when not winner_a then 1 else 0 end,
             losses = losses + case when winner_a then 1 else 0 end,
             last_match_at = ts,
             updated_at = now()
       where player_name = p;
      select elo_rating into after_rating from public.beerpong_player_ratings where player_name = p;
      insert into public.beerpong_player_rating_history(match_id, player_name, rating_before, rating_after, created_at)
      values (m.id, p, coalesce(before_rating,1000), coalesce(after_rating,1000), ts);
    end loop;
  end loop;
end;
$fn$;

create or replace function public.rebuild_klaverjas_ratings()
returns void language plpgsql security definer set search_path to 'public' as $fn$
declare
  g record;
  team1_players text[];
  team2_players text[];
  team1_points integer;
  team2_points integer;
  team1_avg numeric;
  team2_avg numeric;
  expected_1 numeric;
  actual_1 numeric;
  k_factor numeric := 30.0;
  margin_factor numeric;
  delta_total numeric;
  p text;
  before_rating numeric;
  after_rating numeric;
  ts timestamptz;
begin
  truncate table public.klaverjas_player_rating_history restart identity;
  delete from public.klaverjas_player_ratings;

  for g in
    with per_game as (
      select
        j.id,
        j.played_at,
        j.created_at,
        array_agg(e.display_name order by e.seat_no) filter (where e.team_no = 1) as team1_players,
        array_agg(e.display_name order by e.seat_no) filter (where e.team_no = 2) as team2_players,
        max(e.total_points) filter (where e.team_no = 1) as team1_points,
        max(e.total_points) filter (where e.team_no = 2) as team2_points
      from public.jas_games j
      join public.jas_game_entries e on e.game_id = j.id
      where j.deleted_at is null
      group by j.id, j.played_at, j.created_at
    )
    select * from per_game order by played_at, created_at, id
  loop
    team1_players := coalesce(g.team1_players, '{}'::text[]);
    team2_players := coalesce(g.team2_players, '{}'::text[]);
    if coalesce(array_length(team1_players,1),0)=0 or coalesce(array_length(team2_players,1),0)=0 then
      continue;
    end if;
    ts := coalesce(g.created_at, now());
    foreach p in array team1_players loop insert into public.klaverjas_player_ratings(player_name) values (p) on conflict (player_name) do nothing; end loop;
    foreach p in array team2_players loop insert into public.klaverjas_player_ratings(player_name) values (p) on conflict (player_name) do nothing; end loop;
    select avg(elo_rating) into team1_avg from public.klaverjas_player_ratings where player_name = any(team1_players);
    select avg(elo_rating) into team2_avg from public.klaverjas_player_ratings where player_name = any(team2_players);
    expected_1 := public._elo_expected(team1_avg, team2_avg);
    actual_1 := case when coalesce(g.team1_points,0) > coalesce(g.team2_points,0) then 1 when coalesce(g.team1_points,0) < coalesce(g.team2_points,0) then 0 else 0.5 end;
    margin_factor := least(1.5, greatest(1.0, 1.0 + abs(coalesce(g.team1_points,0) - coalesce(g.team2_points,0))::numeric / 600.0));
    delta_total := k_factor * margin_factor * (actual_1 - expected_1);

    foreach p in array team1_players loop
      select elo_rating into before_rating from public.klaverjas_player_ratings where player_name = p;
      update public.klaverjas_player_ratings
         set elo_rating = elo_rating + (delta_total / greatest(coalesce(array_length(team1_players,1),1),1)),
             games_played = games_played + 1,
             wins = wins + case when actual_1 = 1 then 1 else 0 end,
             losses = losses + case when actual_1 = 0 then 1 else 0 end,
             draws = draws + case when actual_1 = 0.5 then 1 else 0 end,
             last_match_at = ts,
             updated_at = now()
       where player_name = p;
      select elo_rating into after_rating from public.klaverjas_player_ratings where player_name = p;
      insert into public.klaverjas_player_rating_history(game_id, player_name, rating_before, rating_after, created_at)
      values (g.id, p, coalesce(before_rating,1000), coalesce(after_rating,1000), ts);
    end loop;

    foreach p in array team2_players loop
      select elo_rating into before_rating from public.klaverjas_player_ratings where player_name = p;
      update public.klaverjas_player_ratings
         set elo_rating = elo_rating - (delta_total / greatest(coalesce(array_length(team2_players,1),1),1)),
             games_played = games_played + 1,
             wins = wins + case when actual_1 = 0 then 1 else 0 end,
             losses = losses + case when actual_1 = 1 then 1 else 0 end,
             draws = draws + case when actual_1 = 0.5 then 1 else 0 end,
             last_match_at = ts,
             updated_at = now()
       where player_name = p;
      select elo_rating into after_rating from public.klaverjas_player_ratings where player_name = p;
      insert into public.klaverjas_player_rating_history(game_id, player_name, rating_before, rating_after, created_at)
      values (g.id, p, coalesce(before_rating,1000), coalesce(after_rating,1000), ts);
    end loop;
  end loop;
end;
$fn$;

create or replace function public.rebuild_boerenbridge_ratings()
returns void language plpgsql security definer set search_path to 'public' as $fn$
declare
  m record;
  rec record;
  names text[];
  p text;
  before_rating numeric;
  after_rating numeric;
  n integer;
  i integer;
  j integer;
  ratings numeric[];
  points integer[];
  exacts integer[];
  slagen integer[];
  deltas numeric[];
  podium_cut integer;
  top_half_cut integer;
  k_factor numeric := 24.0;
  expected_i numeric;
  actual_i numeric;
  ts timestamptz;
begin
  truncate table public.boerenbridge_player_rating_history restart identity;
  delete from public.boerenbridge_player_ratings;
  if to_regclass('public.boerenbridge_player_stats') is not null then
    delete from public.boerenbridge_player_stats;
  end if;

  for m in
    select id, payload, finished_at, started_at, created_at
    from public.boerenbridge_matches
    where deleted_at is null
      and coalesce(match_status, case when nullif(payload->>'finished_at','') is not null then 'finished' else 'draft' end) = 'finished'
    order by coalesce(finished_at, started_at, created_at), id
  loop
    names := '{}'::text[]; ratings := '{}'::numeric[]; points := '{}'::integer[]; exacts := '{}'::integer[]; slagen := '{}'::integer[]; deltas := '{}'::numeric[];
    for rec in
      select
        coalesce(x->>'name','') as player_name,
        coalesce((x->>'final_total_points')::integer,0) as final_total_points,
        coalesce((x->>'exact_bid_count')::integer,0) as exact_bid_count,
        coalesce((x->>'total_slagen')::integer,0) as total_slagen
      from jsonb_array_elements(coalesce(m.payload->'totals','[]'::jsonb)) x
      where nullif(trim(coalesce(x->>'name','')), '') is not null
      order by coalesce((x->>'final_total_points')::integer,0) desc, coalesce(x->>'name','') asc
    loop
      names := array_append(names, rec.player_name);
      points := array_append(points, rec.final_total_points);
      exacts := array_append(exacts, rec.exact_bid_count);
      slagen := array_append(slagen, rec.total_slagen);
      insert into public.boerenbridge_player_ratings(player_name) values (rec.player_name) on conflict (player_name) do nothing;
      select elo_rating into before_rating from public.boerenbridge_player_ratings where player_name = rec.player_name;
      ratings := array_append(ratings, coalesce(before_rating,1000));
      deltas := array_append(deltas, 0);
    end loop;

    n := coalesce(array_length(names,1),0);
    if n < 2 then continue; end if;
    ts := coalesce(m.finished_at, m.started_at, m.created_at, now());
    podium_cut := least(3, n);
    top_half_cut := ceil(n::numeric / 2.0)::integer;

    for i in 1..n loop
      for j in 1..n loop
        if i = j then continue; end if;
        expected_i := public._elo_expected(ratings[i], ratings[j]);
        actual_i := case when points[i] > points[j] then 1 when points[i] < points[j] then 0 else 0.5 end;
        deltas[i] := coalesce(deltas[i],0) + (k_factor / greatest(n - 1, 1)) * (actual_i - expected_i);
      end loop;
    end loop;

    for i in 1..n loop
      p := names[i];
      before_rating := ratings[i];
      update public.boerenbridge_player_ratings
         set elo_rating = elo_rating + deltas[i],
             games_played = games_played + 1,
             wins = wins + case when points[i] = (select max(v) from unnest(points) as v) then 1 else 0 end,
             podium_finishes = podium_finishes + case when i <= podium_cut then 1 else 0 end,
             top_half_finishes = top_half_finishes + case when i <= top_half_cut then 1 else 0 end,
             total_points = total_points + points[i],
             total_exact_bids = total_exact_bids + exacts[i],
             total_slagen = total_slagen + slagen[i],
             last_match_at = ts,
             updated_at = now()
       where player_name = p;
      select elo_rating into after_rating from public.boerenbridge_player_ratings where player_name = p;
      insert into public.boerenbridge_player_rating_history(match_id, player_name, rating_before, rating_after, created_at)
      values (m.id, p, coalesce(before_rating,1000), coalesce(after_rating,1000), ts);
      if to_regclass('public.boerenbridge_player_stats') is not null then
        insert into public.boerenbridge_player_stats(player_name, games_played, wins, total_points, updated_at)
        values (p, 1, case when points[i] = (select max(v) from unnest(points) as v) then 1 else 0 end, points[i], now())
        on conflict (player_name)
        do update set
          games_played = public.boerenbridge_player_stats.games_played + 1,
          wins = public.boerenbridge_player_stats.wins + excluded.wins,
          total_points = public.boerenbridge_player_stats.total_points + excluded.total_points,
          updated_at = now();
      end if;
    end loop;
  end loop;
end;
$fn$;

create or replace function public.process_game_rating_rebuild_queue(p_limit integer default 10)
returns jsonb language plpgsql security definer set search_path to 'public' as $fn$
declare
  q record;
  processed integer := 0;
begin
  for q in
    select id, game_type
    from public.game_rating_rebuild_queue
    where status = 'pending'
    order by requested_at, id
    limit greatest(coalesce(p_limit,10),1)
    for update skip locked
  loop
    update public.game_rating_rebuild_queue set status='processing' where id = q.id;
    begin
      if q.game_type = 'beerpong' then
        perform public.rebuild_beerpong_ratings();
      elsif q.game_type = 'klaverjas' then
        perform public.rebuild_klaverjas_ratings();
      elsif q.game_type = 'boerenbridge' then
        perform public.rebuild_boerenbridge_ratings();
      else
        raise exception 'Onbekend speltype: %', q.game_type;
      end if;
      update public.game_rating_rebuild_queue set status='done', processed_at=now(), last_error=null where id = q.id;
      processed := processed + 1;
    exception when others then
      update public.game_rating_rebuild_queue set status='error', processed_at=now(), last_error=sqlerrm where id = q.id;
      raise;
    end;
  end loop;
  return jsonb_build_object('ok', true, 'processed', processed);
end;
$fn$;

create or replace function public.create_jas_game(session_token text, game_payload jsonb)
returns jsonb language plpgsql security definer set search_path to 'public' as $fn$
declare
  session_player public.players%rowtype;
  game_id_out bigint;
  participant jsonb;
  resolved_player_id bigint;
  payload_participants jsonb;
  participant_count integer;
  distinct_player_count integer;
  seat_arr integer[];
  team_arr integer[];
  missing_name text;
begin
  session_player := public._jas_session_player(session_token);
  payload_participants := coalesce(game_payload -> 'participants', '[]'::jsonb);

  if coalesce(nullif(trim(game_payload ->> 'variant'), ''), '4_player') <> '4_player' then
    raise exception 'Alleen 4-speler klaverjas wordt ondersteund in deze versie';
  end if;
  if coalesce(nullif(trim(game_payload ->> 'scoreboard_mode'), ''), 'teams') <> 'teams' then
    raise exception 'Alleen teamscore wordt ondersteund in deze versie';
  end if;
  if jsonb_typeof(payload_participants) <> 'array' then
    raise exception 'Deelnemers ontbreken';
  end if;
  participant_count := jsonb_array_length(payload_participants);
  if participant_count <> 4 then
    raise exception 'Kies precies 4 geregistreerde spelers';
  end if;
  select count(distinct lower(trim(value ->> 'name'))) into distinct_player_count from jsonb_array_elements(payload_participants) value;
  if distinct_player_count <> 4 then
    raise exception 'Elke speler mag maar één keer gekozen worden';
  end if;
  select array_agg((value ->> 'seat_no')::integer order by (value ->> 'seat_no')::integer),
         array_agg((value ->> 'team_no')::integer order by (value ->> 'seat_no')::integer)
    into seat_arr, team_arr
  from jsonb_array_elements(payload_participants) value;
  if seat_arr is distinct from array[1,2,3,4] then raise exception 'Stoelen moeten 1 t/m 4 zijn'; end if;
  if team_arr is distinct from array[1,2,1,2] then raise exception 'Teams moeten stoel 1+3 en 2+4 gebruiken'; end if;

  for participant in select * from jsonb_array_elements(payload_participants) loop
    select p.id into resolved_player_id from public.players p where lower(p.display_name) = lower(trim(participant ->> 'name')) limit 1;
    if resolved_player_id is null then
      missing_name := coalesce(participant ->> 'name', 'Onbekend');
      raise exception 'Speler niet gevonden of niet geregistreerd: %', missing_name;
    end if;
  end loop;

  insert into public.jas_games(title, played_at, variant, scoreboard_mode, created_by_player_id, created_by_name, payload, updated_at)
  values (
    coalesce(nullif(trim(game_payload ->> 'title'), ''), 'Klaverjas potje'),
    coalesce(nullif(game_payload ->> 'played_at', '')::date, current_date),
    '4_player',
    'teams',
    session_player.id,
    session_player.display_name,
    coalesce(game_payload, '{}'::jsonb),
    now()
  ) returning id into game_id_out;

  for participant in select * from jsonb_array_elements(payload_participants) loop
    select p.id into resolved_player_id from public.players p where lower(p.display_name) = lower(trim(participant ->> 'name')) limit 1;
    insert into public.jas_game_entries(game_id, player_id, display_name, seat_no, team_no, total_points, is_winner)
    values (
      game_id_out,
      resolved_player_id,
      coalesce(nullif(trim(participant ->> 'name'), ''), 'Onbekend'),
      (participant ->> 'seat_no')::integer,
      (participant ->> 'team_no')::integer,
      coalesce((participant ->> 'total_points')::integer, 0),
      coalesce((participant ->> 'is_winner')::boolean, false)
    );
  end loop;

  perform public._enqueue_rating_rebuild('klaverjas', 'new_match', game_id_out::text, 'player', session_player.display_name);
  perform public.process_game_rating_rebuild_queue(10);
  return jsonb_build_object('ok', true, 'game_id', game_id_out);
end;
$fn$;

