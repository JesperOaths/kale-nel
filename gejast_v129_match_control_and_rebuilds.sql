create or replace function public.get_match_control_detail_action(
  game_type_input text,
  match_ref text,
  session_token text default null,
  admin_session_token text default null
) returns jsonb language plpgsql security definer set search_path to 'public' as $fn$
declare
  v_game text := lower(trim(coalesce(game_type_input,'')));
  v_player public.players%rowtype;
  v_admin jsonb;
  out_payload jsonb;
  v_can_edit boolean := false;
begin
  begin if nullif(trim(coalesce(session_token,'')), '') is not null then select * into v_player from public._gejast_player_from_session(session_token); end if; exception when others then null; end;
  begin if nullif(trim(coalesce(admin_session_token,'')), '') is not null then v_admin := to_jsonb(public.admin_check_session(admin_session_token)); end if; exception when others then v_admin := null; end;

  if v_game = 'beerpong' then
    select jsonb_build_object('client_match_id', m.client_match_id, 'payload', m.payload, 'created_at', m.created_at, 'updated_at', m.updated_at, 'deleted_at', m.deleted_at)
      into out_payload
    from public.beerpong_matches m
    where m.client_match_id = match_ref
      and ((v_admin is not null) or (v_player.id is not null and m.created_by_player_id = v_player.id));
    select (v_admin is not null) or (v_player.id is not null and m.created_by_player_id = v_player.id and now() <= m.created_at + interval '5 minutes') into v_can_edit from public.beerpong_matches m where m.client_match_id = match_ref;
  elsif v_game = 'boerenbridge' then
    select jsonb_build_object('client_match_id', m.client_match_id, 'payload', m.payload, 'created_at', m.created_at, 'updated_at', m.updated_at, 'deleted_at', m.deleted_at)
      into out_payload
    from public.boerenbridge_matches m
    where m.client_match_id = match_ref
      and ((v_admin is not null) or (v_player.id is not null and m.created_by_player_id = v_player.id));
    select (v_admin is not null) or (v_player.id is not null and m.created_by_player_id = v_player.id and now() <= m.created_at + interval '5 minutes') into v_can_edit from public.boerenbridge_matches m where m.client_match_id = match_ref;
  elsif v_game = 'klaverjas' then
    select jsonb_build_object('match_ref', j.id::text, 'payload', j.payload, 'created_at', j.created_at, 'updated_at', j.updated_at, 'deleted_at', j.deleted_at)
      into out_payload
    from public.jas_games j
    where j.id = match_ref::bigint
      and ((v_admin is not null) or (v_player.id is not null and j.created_by_player_id = v_player.id));
    select (v_admin is not null) or (v_player.id is not null and j.created_by_player_id = v_player.id and now() <= j.created_at + interval '5 minutes') into v_can_edit from public.jas_games j where j.id = match_ref::bigint;
