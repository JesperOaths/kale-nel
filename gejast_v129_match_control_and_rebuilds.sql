create or replace function public._elo_expected(rating_a numeric, rating_b numeric)
returns numeric language sql immutable as $$
  select 1.0 / (1.0 + power(10.0, (coalesce(rating_b,1000) - coalesce(rating_a,1000)) / 400.0));
$$;

