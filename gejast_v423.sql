create or replace function public.admin_queue_activation_email(
  admin_session_token text,
  request_id_input bigint,
  base_url text,
  recipient_email_input text default null,
  recipient_name_input text default null,
  activation_link_input text default null,
  player_id_input bigint default null
)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  admin_row record;
  req public.claim_requests%rowtype;
  activation_payload jsonb;
  activation_url text;
  recipient_email_clean text;
  recipient_name_clean text;
  job_id_out bigint;
begin
  select * into admin_row from public._require_valid_admin_session(admin_session_token);

  select * into req
  from public.claim_requests
  where id = request_id_input
  limit 1;

  if not found then
    raise exception 'Aanvraag niet gevonden';
  end if;

  if lower(coalesce(req.status, '')) <> 'approved' then
    raise exception 'Alleen goedgekeurde aanvragen kunnen in de mailqueue';
  end if;

  activation_url := nullif(trim(coalesce(activation_link_input, '')), '');

  if activation_url is null then
    activation_payload := public.create_player_activation_link(admin_session_token, request_id_input, base_url);
    activation_url := activation_payload ->> 'activation_url';
  else
    activation_payload := jsonb_build_object(
      'request_id', req.id,
      'display_name', coalesce(public._claim_request_display_name(req.id), null),
      'requester_email', coalesce(nullif(trim(recipient_email_input), ''), lower(trim(req.requester_email))),
      'activation_url', activation_url
    );
  end if;

  recipient_email_clean := lower(trim(coalesce(nullif(recipient_email_input, ''), req.requester_email)));
  recipient_name_clean := nullif(trim(coalesce(recipient_name_input, activation_payload ->> 'display_name', public._claim_request_display_name(req.id))), '');

  if recipient_email_clean is null or recipient_email_clean = '' then
    raise exception 'Ontvanger-e-mail ontbreekt';
  end if;

  insert into public.outbound_email_jobs (
    job_type, job_status, recipient_email, recipient_name, subject, payload,
    requested_by_admin_id, related_claim_request_id, related_player_id
  )
  values (
    'activation_email',
    'queued',
    recipient_email_clean,
    recipient_name_clean,
    'Activeer je account',
    jsonb_build_object(
      'request_id', req.id,
      'activation_url', activation_url,
      'display_name', coalesce(recipient_name_clean, activation_payload ->> 'display_name'),
      'requester_email', recipient_email_clean,
      'base_url', regexp_replace(trim(base_url), '/+$', ''),
      'template', 'activation_email_v1'
    ),
    admin_row.id,
    req.id,
    coalesce(player_id_input, req.player_id)
  )
  returning id into job_id_out;

  return jsonb_build_object(
    'queued', true,
    'job_id', job_id_out,
    'request_id', req.id,
    'recipient_email', recipient_email_clean,
    'recipient_name', recipient_name_clean,
    'activation_url', activation_url,
    'delivery_mode', 'outbound_email_jobs'
  );
end;
$$;

