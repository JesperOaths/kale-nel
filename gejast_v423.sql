  lat_input double precision default null,
  lng_input double precision default null,
  accuracy_input double precision default null,
  user_agent_input text default null
) returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  v_player_id bigint := public._gejast_player_id_from_session(session_token);
  v_name text := public._gejast_player_name_from_session(session_token);
  v_scope text := coalesce(nullif(trim(coalesce(site_scope_input,'')), ''), public._web_push_scope_for_player(v_player_id), 'friends');
