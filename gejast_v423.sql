create or replace function public.get_gejast_homepage_state(
  session_token text default null
)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  base_state jsonb;
  can_count integer;
begin
  base_state := public.get_public_state(session_token);
  can_count := coalesce((base_state ->> 'can_count')::integer, (base_state ->> 'yes_count')::integer, 0);

  if coalesce(base_state ->> 'status_key', '') <> '' and coalesce(base_state ->> 'status_label', '') <> '' then
    return base_state;
  end if;

  return coalesce(base_state, '{}'::jsonb)
    || jsonb_build_object(
      'status_key',
      case when can_count >= 4 then 'yes' when can_count = 3 then 'maybe' else 'no' end,
      'status_label',
      case when can_count >= 4 then 'Ja' when can_count = 3 then 'Bijna' else 'Nee' end
    );
end;
$$;

