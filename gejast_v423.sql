create or replace function public.admin_get_site_analytics_action(
  admin_session_token text,
  range_days integer default 7,
  recent_limit integer default 80
)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $function$
declare
  v_session jsonb;
  v_days integer := greatest(1, least(coalesce(range_days, 7), 365));
  v_recent_limit integer := greatest(1, least(coalesce(recent_limit, 80), 250));
  v_since timestamptz := now() - make_interval(days => v_days);
begin
  v_session := to_jsonb(public.admin_check_session(admin_session_token));
  if coalesce((v_session->>'ok')::boolean, false) is not true then
    raise exception 'Log opnieuw in als admin.';
  end if;

  return jsonb_build_object(
    'ok', true,
    'admin_session_token', coalesce(v_session->>'admin_session_token', admin_session_token),
    'summary', (
      select jsonb_build_object(
        'total_views', count(*)::int,
        'unique_visitors', count(distinct e.visitor_id)::int,
        'unique_sessions', count(distinct e.session_id)::int,
        'range_days', v_days
      )
      from public.site_visitor_events e
      where e.created_at >= v_since
    ),
    'top_pages', coalesce((
      select jsonb_agg(jsonb_build_object('page_path', x.page_path, 'views', x.views) order by x.views desc, x.page_path)
      from (
        select e.page_path, count(*)::int as views
        from public.site_visitor_events e
        where e.created_at >= v_since
        group by e.page_path
        order by views desc, e.page_path
        limit 12
      ) x
    ), '[]'::jsonb),
    'devices', coalesce((
      select jsonb_agg(jsonb_build_object('device_type', x.device_type, 'views', x.views) order by x.views desc, x.device_type)
      from (
        select coalesce(nullif(e.device_type,''), 'Onbekend') as device_type, count(*)::int as views
        from public.site_visitor_events e
        where e.created_at >= v_since
        group by 1
        order by views desc, 1
        limit 8
      ) x
    ), '[]'::jsonb),
    'browsers', coalesce((
      select jsonb_agg(jsonb_build_object('browser_name', x.browser_name, 'views', x.views) order by x.views desc, x.browser_name)
      from (
        select coalesce(nullif(e.browser_name,''), 'Onbekend') as browser_name, count(*)::int as views
        from public.site_visitor_events e
        where e.created_at >= v_since
        group by 1
        order by views desc, 1
        limit 8
      ) x
    ), '[]'::jsonb),
    'referrers', coalesce((
      select jsonb_agg(jsonb_build_object('referrer_url', x.referrer_url, 'views', x.views) order by x.views desc)
      from (
        select coalesce(nullif(e.referrer_url,''), 'Direct') as referrer_url, count(*)::int as views
        from public.site_visitor_events e
        where e.created_at >= v_since
        group by 1
        order by views desc, 1
        limit 10
      ) x
    ), '[]'::jsonb),
    'recent_events', coalesce((
      select jsonb_agg(jsonb_build_object(
        'created_at', x.created_at,
        'page_path', x.page_path,
        'device_type', x.device_type,
        'browser_name', x.browser_name,
        'referrer_url', x.referrer_url
      ) order by x.created_at desc)
      from (
        select e.created_at, e.page_path,
               coalesce(nullif(e.device_type,''), 'Onbekend') as device_type,
               coalesce(nullif(e.browser_name,''), 'Onbekend') as browser_name,
               coalesce(nullif(e.referrer_url,''), 'Direct') as referrer_url
        from public.site_visitor_events e
        where e.created_at >= v_since
        order by e.created_at desc
        limit v_recent_limit
      ) x
    ), '[]'::jsonb)
  );
end;
$function$;

