create or replace function public.get_jas_app_state(session_token text default null)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  base_state jsonb;
  recent_games jsonb;
  registered_names jsonb;
begin
  base_state := public.get_public_state(session_token);

  select coalesce(jsonb_agg(p.display_name order by lower(p.display_name)), '[]'::jsonb)
    into registered_names
    from public.players p;

  select coalesce(jsonb_agg(game_row order by played_at desc, created_at desc), '[]'::jsonb)
    into recent_games
    from (
      select
        g.id,
        g.title,
        g.played_at,
        g.variant,
        g.scoreboard_mode,
        g.created_at,
        coalesce((
          select jsonb_agg(e.display_name order by e.seat_no, e.id)
          from public.jas_game_entries e
          where e.game_id = g.id
        ), '[]'::jsonb) as players,
        coalesce((
          select jsonb_agg(e.display_name order by e.seat_no, e.id)
          from public.jas_game_entries e
          where e.game_id = g.id and e.is_winner = true
        ), '[]'::jsonb) as winners
      from public.jas_games g
      order by g.played_at desc, g.created_at desc
      limit 12
    ) game_row;

  return jsonb_build_object(
    'all_names', registered_names,
    'my_name', base_state ->> 'my_name',
    'recent_games', recent_games
  );
end;
$$;


