begin;

create extension if not exists pgcrypto with schema extensions;

create table if not exists public.player_activation_links (
  id bigint generated by default as identity primary key,
  claim_request_id bigint not null unique references public.claim_requests(id) on delete cascade,
  player_id bigint not null references public.players(id) on delete cascade,
  display_name text not null,
  requester_email text not null,
  token_hash text not null unique,
  expires_at timestamptz not null,
  used_at timestamptz null,
  created_at timestamptz not null default now(),
  last_sent_at timestamptz null
);

create index if not exists player_activation_links_player_idx
  on public.player_activation_links(player_id);

create index if not exists player_activation_links_email_idx
  on public.player_activation_links(lower(requester_email));

create or replace function public._hash_activation_token(raw_token text)
returns text
language sql
immutable
as $$
  select encode(extensions.digest(raw_token, 'sha256'), 'hex');
$$;

create or replace function public.create_player_activation_link(
  admin_session_token text,
  request_id_input bigint,
  base_url text default 'https://kalenel.nl/activate.html'
)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $function$
declare
  sess record;
  req public.claim_requests%rowtype;
  player_row public.players%rowtype;
  raw_token text;
  hashed_token text;
  clean_base_url text;
  expires_at_out timestamptz;
begin
  select * into sess
  from public._require_valid_admin_session(admin_session_token);

  if coalesce(sess.ok, false) is not true then
    raise exception 'Ongeldige admin-sessie';
  end if;

  select *
    into req
    from public.claim_requests
   where id = request_id_input;

  if not found then
    raise exception 'Aanvraag niet gevonden';
  end if;

  if lower(coalesce(req.status, '')) <> 'approved' then
    raise exception 'Activatielink kan alleen voor goedgekeurde aanvragen worden gemaakt';
  end if;

  if req.requester_email is null or trim(req.requester_email) = '' then
    raise exception 'Deze aanvraag heeft geen e-mailadres';
  end if;

  select *
    into player_row
    from public.players
   where id = req.player_id
      or lower(display_name) = lower(coalesce(public._claim_request_display_name(req.id), ''))
   order by case when id = req.player_id then 0 else 1 end
   limit 1;

  if not found then
    raise exception 'Geen speler gevonden voor deze goedgekeurde aanvraag';
  end if;

  raw_token := encode(extensions.gen_random_bytes(24), 'hex');
  hashed_token := public._hash_activation_token(raw_token);
  clean_base_url := regexp_replace(trim(base_url), '/+$', '');
  expires_at_out := now() + interval '24 hours';

  insert into public.player_activation_links (
    claim_request_id,
    player_id,
    display_name,
    requester_email,
    token_hash,
    expires_at,
    last_sent_at
  )
  values (
    req.id,
    player_row.id,
    player_row.display_name,
    lower(trim(req.requester_email)),
    hashed_token,
    expires_at_out,
    now()
  )
  on conflict (claim_request_id)
  do update set
    player_id = excluded.player_id,
    display_name = excluded.display_name,
    requester_email = excluded.requester_email,
    token_hash = excluded.token_hash,
    expires_at = excluded.expires_at,
    used_at = null,
    last_sent_at = now();

  return jsonb_build_object(
    'request_id', req.id,
    'player_id', player_row.id,
    'display_name', player_row.display_name,
    'requester_email', lower(trim(req.requester_email)),
    'activation_url', clean_base_url || '?token=' || raw_token,
    'expires_at', expires_at_out
  );
end;
$function$;

create or replace function public.get_activation_link_context(
  activation_token text
)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $function$
declare
  link_row public.player_activation_links%rowtype;
begin
  if activation_token is null or trim(activation_token) = '' then
    raise exception 'Activatietoken ontbreekt';
  end if;

  select *
    into link_row
    from public.player_activation_links
   where token_hash = public._hash_activation_token(trim(activation_token))
   limit 1;

  if not found then
    raise exception 'Activatielink ongeldig';
  end if;

  if link_row.used_at is not null then
    raise exception 'Deze activatielink is al gebruikt';
  end if;

  if link_row.expires_at < now() then
    raise exception 'Deze activatielink is verlopen';
  end if;

  return jsonb_build_object(
    'display_name', link_row.display_name,
    'requester_email', link_row.requester_email,
    'expires_at', link_row.expires_at
  );
end;
$function$;

create or replace function public.activate_player_from_email_link(
  activation_token text,
  new_pin text
)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $function$
declare
  link_row public.player_activation_links%rowtype;
  player_row public.players%rowtype;
begin
  if activation_token is null or trim(activation_token) = '' then
    raise exception 'Activatietoken ontbreekt';
  end if;

  if new_pin is null or new_pin !~ '^[0-9]{4}$' then
    raise exception 'Pincode moet precies 4 cijfers zijn';
  end if;

  select *
    into link_row
    from public.player_activation_links
   where token_hash = public._hash_activation_token(trim(activation_token))
   limit 1;

  if not found then
    raise exception 'Activatielink ongeldig';
  end if;

  if link_row.used_at is not null then
    raise exception 'Deze activatielink is al gebruikt';
  end if;

  if link_row.expires_at < now() then
    raise exception 'Deze activatielink is verlopen';
  end if;

  select *
    into player_row
    from public.players
   where id = link_row.player_id
   limit 1;

  if not found then
    raise exception 'Speler niet gevonden';
  end if;

  if lower(player_row.display_name) <> lower(link_row.display_name) then
    raise exception 'Naam komt niet overeen met deze activatielink';
  end if;

  if player_row.pin_hash is not null then
    raise exception 'Deze speler is al geactiveerd';
  end if;

  update public.players
     set pin_hash = extensions.crypt(new_pin, extensions.gen_salt('bf')),
         approved = true,
         active = true,
         approved_email = link_row.requester_email
   where id = player_row.id;

  update public.player_activation_links
     set used_at = now()
   where id = link_row.id;

  return public.login_player(player_row.display_name, new_pin);
end;
$function$;

create or replace function public.claim_outbound_email_jobs(p_limit integer default 10)
returns setof public.outbound_email_jobs
language plpgsql
security definer
set search_path to 'public'
as $$
begin
  return query
  update public.outbound_email_jobs j
     set job_status = 'processing',
         status = 'processing',
         updated_at = now()
   where j.id in (
     select id
     from public.outbound_email_jobs
     where job_status = 'pending'
       and coalesce(attempts, 0) < 5
     order by created_at asc
     limit p_limit
     for update skip locked
   )
   returning *;
end;
$$;

create or replace function public.claim_email_jobs_http(p_limit int default 5)
returns json
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  v_result json;
begin
  select coalesce(json_agg(t), '[]'::json)
    into v_result
  from public.claim_outbound_email_jobs(p_limit) t;

  return v_result;
end;
$$;

create or replace function public.mark_outbound_email_job_sent(
  p_job_id bigint,
  p_provider_message_id text default null
)
returns public.outbound_email_jobs
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  v_row public.outbound_email_jobs;
begin
  update public.outbound_email_jobs
     set job_status = 'sent',
         status = 'sent',
         sent_at = now(),
         updated_at = now(),
         provider_message_id = coalesce(p_provider_message_id, provider_message_id),
         last_error = null
   where id = p_job_id
   returning * into v_row;

  return v_row;
end;
$$;

create or replace function public.mark_outbound_email_job_failed(
  p_job_id bigint,
  p_error text
)
returns public.outbound_email_jobs
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  v_row public.outbound_email_jobs;
begin
  update public.outbound_email_jobs
     set job_status = case
           when coalesce(attempts, 0) + 1 >= 5 then 'failed'
           else 'pending'
         end,
         status = case
           when coalesce(attempts, 0) + 1 >= 5 then 'failed'
           else 'pending'
         end,
         attempts = coalesce(attempts, 0) + 1,
         last_error = left(coalesce(p_error, 'unknown error'), 2000),
         updated_at = now()
   where id = p_job_id
   returning * into v_row;

  return v_row;
end;
$$;

create or replace function public.retry_outbound_email_job(p_job_id bigint)
returns public.outbound_email_jobs
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  v_row public.outbound_email_jobs;
begin
  update public.outbound_email_jobs
     set job_status = 'pending',
         status = 'pending',
         last_error = null,
         updated_at = now()
   where id = p_job_id
   returning * into v_row;

  return v_row;
end;
$$;

create or replace function public.admin_retry_outbound_email_job(
  admin_session_token text,
  p_job_id bigint
)
returns public.outbound_email_jobs
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  admin_row record;
  v_row public.outbound_email_jobs;
begin
  select * into admin_row
  from public._require_valid_admin_session(admin_session_token);

  if coalesce(admin_row.ok, false) is not true then
    raise exception 'Ongeldige admin-sessie';
  end if;

  select * into v_row
  from public.retry_outbound_email_job(p_job_id);

  return v_row;
end;
$$;

create or replace view public.outbound_email_jobs_recent as
select *
from public.outbound_email_jobs
order by created_at desc;

create or replace function public.admin_queue_activation_email(
  admin_session_token text,
  request_id_input bigint,
  base_url text,
  recipient_email_input text default null,
  recipient_name_input text default null,
  activation_link_input text default null,
  player_id_input bigint default null
)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  admin_row record;
  req public.claim_requests%rowtype;
  activation_payload jsonb;
  activation_url text;
  expires_at_out timestamptz;
  recipient_email_clean text;
  recipient_name_clean text;
  job_id_out bigint;
begin
  select * into admin_row
  from public._require_valid_admin_session(admin_session_token);

  if coalesce(admin_row.ok, false) is not true then
    raise exception 'Ongeldige admin-sessie';
  end if;

  select *
    into req
    from public.claim_requests
   where id = request_id_input
   limit 1;

  if not found then
    raise exception 'Aanvraag niet gevonden';
  end if;

  if lower(coalesce(req.status, '')) <> 'approved' then
    raise exception 'Alleen goedgekeurde aanvragen kunnen in de mailqueue';
  end if;

  activation_url := nullif(trim(coalesce(activation_link_input, '')), '');

  if activation_url is null then
    activation_payload := public.create_player_activation_link(
      admin_session_token,
      request_id_input,
      base_url
    );
    activation_url := activation_payload ->> 'activation_url';
    expires_at_out := (activation_payload ->> 'expires_at')::timestamptz;
  else
    activation_payload := jsonb_build_object(
      'request_id', req.id,
      'player_id', coalesce(player_id_input, req.player_id),
      'display_name', coalesce(public._claim_request_display_name(req.id), null),
      'requester_email', coalesce(nullif(trim(recipient_email_input), ''), lower(trim(req.requester_email))),
      'activation_url', activation_url,
      'expires_at', now() + interval '24 hours'
    );
    expires_at_out := (activation_payload ->> 'expires_at')::timestamptz;
  end if;

  recipient_email_clean := lower(trim(coalesce(nullif(recipient_email_input, ''), req.requester_email)));
  recipient_name_clean := nullif(trim(coalesce(
    recipient_name_input,
    activation_payload ->> 'display_name',
    public._claim_request_display_name(req.id)
  )), '');

  if recipient_email_clean is null or recipient_email_clean = '' then
    raise exception 'Ontvanger-e-mail ontbreekt';
  end if;

  insert into public.outbound_email_jobs (
    job_type,
    status,
    job_status,
    recipient_email,
    recipient_name,
    subject,
    payload,
    attempts,
    requested_by_admin_id,
    related_claim_request_id,
    related_player_id
  )
  values (
    'activation_email',
    'pending',
    'pending',
    recipient_email_clean,
    recipient_name_clean,
    'Activeer je account voor Wordt er gejast donderdag?',
    jsonb_build_object(
      'request_id', req.id,
      'player_id', coalesce(player_id_input, req.player_id),
      'display_name', coalesce(recipient_name_clean, activation_payload ->> 'display_name'),
      'requester_email', recipient_email_clean,
      'activation_url', activation_url,
      'expires_at', expires_at_out,
      'base_url', regexp_replace(trim(base_url), '/+$', ''),
      'template', 'activation_email_v2'
    ),
    0,
    admin_row.id,
    req.id,
    coalesce(player_id_input, req.player_id)
  )
  returning id into job_id_out;

  return jsonb_build_object(
    'queued', true,
    'job_id', job_id_out,
    'request_id', req.id,
    'recipient_email', recipient_email_clean,
    'recipient_name', recipient_name_clean,
    'activation_url', activation_url,
    'expires_at', expires_at_out
  );
end;
$$;

grant execute on function public.create_player_activation_link(text, bigint, text) to anon, authenticated;
