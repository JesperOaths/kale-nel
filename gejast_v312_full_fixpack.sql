  delete from public.site_poll_votes
   where poll_key = v_poll
     and (
       public._gejast_norm_name(display_name) = public._gejast_norm_name(v_name)
       or (v_player_id is not null and player_id = v_player_id)
     );

  insert into public.site_poll_votes(poll_key, player_id, display_name, desired_status, created_at, updated_at)
  values (v_poll, v_player_id, v_name, v_status, now(), now());

  return public.get_site_poll_state(v_poll, session_token);
end;
