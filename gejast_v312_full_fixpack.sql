create or replace function public.admin_get_active_web_push_presence(
  admin_session_token text,
  seen_within_minutes integer default 5
) returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_items jsonb := '[]'::jsonb;
  v_cutoff timestamptz := now() - make_interval(mins => greatest(coalesce(seen_within_minutes,5), 1));
begin
  perform public.admin_check_session(admin_session_token);

  select coalesce(jsonb_agg(row_json order by last_seen_at desc, display_name asc), '[]'::jsonb)
    into v_items
  from (
    select
      p.display_name,
      p.player_id,
      max(p.last_seen_at) as last_seen_at,
      count(*)::int as device_count,
      jsonb_agg(distinct jsonb_build_object('endpoint', p.endpoint, 'platform', p.platform, 'permission_state', p.permission_state)) as devices,
      jsonb_build_object(
        'display_name', p.display_name,
        'player_id', p.player_id,
        'last_seen_at', max(p.last_seen_at),
        'device_count', count(*)::int,
        'devices', jsonb_agg(distinct jsonb_build_object('endpoint', p.endpoint, 'platform', p.platform, 'permission_state', p.permission_state))
      ) as row_json
    from public.web_push_active_presence p
    where p.last_seen_at >= v_cutoff
    group by p.display_name, p.player_id
  ) grouped;

  return jsonb_build_object('ok', true, 'count', jsonb_array_length(v_items), 'targets', v_items, 'items', v_items);
end;
$fn$;

