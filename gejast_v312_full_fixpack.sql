create or replace function public._gejast_summary_rows(include_live boolean default true)
returns table(
  game_key text,
  match_ref text,
  created_at timestamptz,
  finished_at timestamptz,
  participants text[],
  winner_names text[],
  recap_text text,
  summary_payload jsonb,
  is_live boolean
)
language sql
stable
as $$
  with rows as (
    select
      lower(s.game_type) as game_key,
      s.client_match_id as match_ref,
      s.created_at,
      s.finished_at,
      public._gejast_summary_participants(s.summary_payload, s.participant_names) as participants,
      public._gejast_summary_winners(s.summary_payload, s.winner_names) as winner_names,
      coalesce(nullif(trim(coalesce(s.summary_payload->>'recap_text','')), ''), s.recap_text) as recap_text,
      coalesce(s.summary_payload, '{}'::jsonb) as summary_payload,
      (s.finished_at is null) as is_live
    from public.game_match_summaries s
    where lower(coalesce(s.game_type,'')) in ('klaverjas','boerenbridge')
  )
  select *
  from rows r
  where (
    r.game_key = 'klaverjas'
    and coalesce(array_length(r.participants,1),0) = 4
    and (include_live or r.finished_at is not null)
    and (r.is_live or public._gejast_round_count(r.summary_payload) > 0)
  )
  or (
    r.game_key = 'boerenbridge'
    and coalesce(array_length(r.participants,1),0) >= 3
    and (include_live or r.finished_at is not null)
    and (r.is_live or public._gejast_round_count(r.summary_payload) > 0)
  )
$$;

