create or replace function public.get_site_poll_state(
  poll_key_input text,
  session_token text default null
) returns jsonb
language plpgsql
stable
security definer
set search_path to 'public'
as $fn$
declare
  v_poll text := coalesce(nullif(trim(coalesce(poll_key_input,'')), ''), 'gezopen_vanavond');
  v_my_name text := public._gejast_player_name_from_session(session_token);
  v_yes jsonb := '[]'::jsonb;
  v_no jsonb := '[]'::jsonb;
  v_my_status text;
begin
  select coalesce(jsonb_agg(spv.display_name order by lower(spv.display_name)), '[]'::jsonb)
    into v_yes
  from public.site_poll_votes spv
  where spv.poll_key = v_poll and lower(coalesce(spv.desired_status,'')) = 'ja';

  select coalesce(jsonb_agg(spv.display_name order by lower(spv.display_name)), '[]'::jsonb)
    into v_no
  from public.site_poll_votes spv
  where spv.poll_key = v_poll and lower(coalesce(spv.desired_status,'')) = 'nee';

  if v_my_name is not null then
    select spv.desired_status into v_my_status
    from public.site_poll_votes spv
    where spv.poll_key = v_poll
      and public._gejast_norm_name(spv.display_name) = public._gejast_norm_name(v_my_name)
    order by spv.updated_at desc, spv.id desc
    limit 1;
  end if;

  return jsonb_build_object(
    'poll_key', v_poll,
    'yes_names', v_yes,
    'no_names', v_no,
    'legends', v_yes,
    'losers', v_no,
    'yes_count', jsonb_array_length(v_yes),
    'no_count', jsonb_array_length(v_no),
    'can_count', jsonb_array_length(v_yes),
    'my_name', v_my_name,
    'my_status', v_my_status
  );
end;
$fn$;

create or replace function public.cast_site_poll_vote(
  poll_key_input text,
  session_token text,
  desired_status text
) returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
