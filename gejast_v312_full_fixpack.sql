create or replace function public.mark_web_push_job_failed(job_id_input bigint, error_input text)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_sub_id bigint;
  v_err text := left(coalesce(error_input,''), 1200);
begin
  update public.web_push_jobs
     set status = 'failed',
         failed_at = now(),
         updated_at = now(),
         error_text = v_err
   where id = job_id_input
   returning target_subscription_id into v_sub_id;

  if v_sub_id is not null and (v_err ilike '%410%' or v_err ilike '%404%' or v_err ilike '%unsub%' or v_err ilike '%gone%') then
    update public.web_push_subscriptions
       set disabled_at = now(),
           last_error = v_err,
           updated_at = now()
     where id = v_sub_id;
  elsif v_sub_id is not null then
    update public.web_push_subscriptions
       set last_error = v_err,
           updated_at = now()
     where id = v_sub_id;
  end if;

  return jsonb_build_object('ok', true, 'job_id', job_id_input);
