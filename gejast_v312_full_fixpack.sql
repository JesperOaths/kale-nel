create or replace function public.claim_web_push_jobs(max_jobs integer default 25)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_items jsonb := '[]'::jsonb;
begin
  with picked as (
    select j.id
    from public.web_push_jobs j
    join public.web_push_subscriptions s on s.id = j.target_subscription_id
    where j.status = 'pending'
      and s.disabled_at is null
    order by j.created_at asc, j.id asc
    limit greatest(coalesce(max_jobs,25),1)
    for update of j skip locked
  ), claimed as (
    update public.web_push_jobs j
       set status = 'claimed',
           claimed_at = now(),
           updated_at = now()
     where j.id in (select id from picked)
     returning j.*
  )
  select coalesce(jsonb_agg(jsonb_build_object(
    'job_id', c.id,
    'title', c.title,
    'body', c.body,
    'target_url', c.target_url,
    'endpoint', s.endpoint,
    'p256dh_key', s.p256dh_key,
    'auth_key', s.auth_key,
    'display_name', s.display_name
  ) order by c.created_at asc, c.id asc), '[]'::jsonb)
    into v_items
  from claimed c
  join public.web_push_subscriptions s on s.id = c.target_subscription_id;

  return jsonb_build_object('items', v_items);
end;
$fn$;

