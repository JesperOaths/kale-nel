create or replace function public.register_web_push_subscription(
  session_token text,
  endpoint_input text,
  p256dh_input text,
  auth_input text,
  user_agent_input text default null,
  permission_input text default null,
  platform_input text default null
) returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_player_id bigint := public._gejast_player_id_from_session(session_token);
  v_name text := public._gejast_player_name_from_session(session_token);
  v_endpoint text := nullif(trim(coalesce(endpoint_input,'')), '');
  v_row public.web_push_subscriptions%rowtype;
begin
  if v_player_id is null or v_name is null then
    raise exception 'Log opnieuw in voor meldingen.';
  end if;
  if v_endpoint is null then
    raise exception 'Push endpoint ontbreekt';
  end if;
  if nullif(trim(coalesce(p256dh_input,'')), '') is null or nullif(trim(coalesce(auth_input,'')), '') is null then
    raise exception 'Push sleutelgegevens ontbreken';
  end if;

  insert into public.web_push_subscriptions(player_id, display_name, endpoint, p256dh_key, auth_key, user_agent, permission_state, platform, created_at, updated_at, last_seen_at, disabled_at, last_error)
  values (v_player_id, v_name, v_endpoint, p256dh_input, auth_input, nullif(trim(coalesce(user_agent_input,'')), ''), nullif(trim(coalesce(permission_input,'')), ''), nullif(trim(coalesce(platform_input,'')), ''), now(), now(), now(), null, null)
  on conflict (endpoint)
  do update set
    player_id = excluded.player_id,
    display_name = excluded.display_name,
    p256dh_key = excluded.p256dh_key,
    auth_key = excluded.auth_key,
    user_agent = excluded.user_agent,
    permission_state = excluded.permission_state,
    platform = excluded.platform,
    updated_at = now(),
    last_seen_at = now(),
    disabled_at = null,
    last_error = null
  returning * into v_row;

  return jsonb_build_object('ok', true, 'subscription_id', v_row.id, 'display_name', v_name);
end;
$fn$;

create or replace function public.touch_active_web_push_presence(
  session_token text,
  endpoint_input text default null,
  permission_input text default null,
  user_agent_input text default null,
  platform_input text default null
) returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_player_id bigint := public._gejast_player_id_from_session(session_token);
  v_name text := public._gejast_player_name_from_session(session_token);
  v_endpoint text := nullif(trim(coalesce(endpoint_input,'')), '');
begin
  if v_player_id is null or v_name is null then
    raise exception 'Log opnieuw in voor actieve meldingen.';
  end if;

  if v_endpoint is null then
    select s.endpoint into v_endpoint
    from public.web_push_subscriptions s
    where s.player_id = v_player_id and s.disabled_at is null
    order by s.last_seen_at desc, s.id desc
    limit 1;
  end if;

  if v_endpoint is not null then
    update public.web_push_subscriptions
       set last_seen_at = now(),
           updated_at = now(),
           permission_state = coalesce(nullif(trim(coalesce(permission_input,'')), ''), permission_state),
           user_agent = coalesce(nullif(trim(coalesce(user_agent_input,'')), ''), user_agent),
           platform = coalesce(nullif(trim(coalesce(platform_input,'')), ''), platform),
           disabled_at = null
     where endpoint = v_endpoint;
  end if;

  delete from public.web_push_active_presence
   where player_id = v_player_id
     and ((endpoint is null and v_endpoint is null) or endpoint = v_endpoint);

  insert into public.web_push_active_presence(player_id, display_name, endpoint, platform, permission_state, user_agent, created_at, updated_at, last_seen_at)
  values (v_player_id, v_name, v_endpoint, nullif(trim(coalesce(platform_input,'')), ''), nullif(trim(coalesce(permission_input,'')), ''), nullif(trim(coalesce(user_agent_input,'')), ''), now(), now(), now());

  return jsonb_build_object('ok', true, 'display_name', v_name, 'endpoint', v_endpoint);
end;
