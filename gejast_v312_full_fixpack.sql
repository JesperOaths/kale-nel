create or replace function public._gejast_norm_name(name_input text)
returns text
language sql
immutable
as $$
  select lower(regexp_replace(trim(coalesce(name_input,'')), '\s+', ' ', 'g'))
$$;

create or replace function public._gejast_round_count(payload jsonb)
returns integer
language sql
immutable
as $$
  select case
    when jsonb_typeof(coalesce(payload,'{}'::jsonb)->'rounds') = 'array' then jsonb_array_length(coalesce(payload,'{}'::jsonb)->'rounds')
    else 0
  end
$$;

create or replace function public._gejast_summary_participants(payload jsonb, fallback text[] default null)
returns text[]
language sql
immutable
as $$
  with src as (
    select case
      when jsonb_typeof(coalesce(payload,'{}'::jsonb)->'participants') = 'array' then coalesce(payload,'{}'::jsonb)->'participants'
      when jsonb_typeof(coalesce(payload,'{}'::jsonb)->'players') = 'array' then coalesce(payload,'{}'::jsonb)->'players'
      else '[]'::jsonb
    end as j
  )
  select coalesce(
    array_agg(trim(v.value::text)) filter (where nullif(trim(v.value::text),'') is not null),
    coalesce(fallback, '{}'::text[])
  )
  from src
  left join lateral jsonb_array_elements_text(src.j) as v(value) on true
$$;

create or replace function public._gejast_summary_winners(payload jsonb, fallback text[] default null)
returns text[]
language sql
immutable
as $$
  with src as (
    select case
      when jsonb_typeof(coalesce(payload,'{}'::jsonb)->'winner_names') = 'array' then coalesce(payload,'{}'::jsonb)->'winner_names'
      else '[]'::jsonb
    end as j
  )
  select coalesce(
    array_agg(trim(v.value::text)) filter (where nullif(trim(v.value::text),'') is not null),
    coalesce(fallback, '{}'::text[])
  )
  from src
  left join lateral jsonb_array_elements_text(src.j) as v(value) on true
$$;

