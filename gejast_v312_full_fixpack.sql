        coalesce(sc.total_summary_matches,0) + coalesce(bp.beerpong_matches,0),
        case when (coalesce(sc.total_summary_matches,0) + coalesce(bp.beerpong_matches,0)) > 0 then
          (100.0 * (coalesce(sc.total_summary_wins,0) + coalesce(bp.beerpong_wins,0))) / (coalesce(sc.total_summary_matches,0) + coalesce(bp.beerpong_matches,0))
        else 0 end
      ) as best_badge
    from public.players p
    left join public.player_profiles pp on pp.player_id = p.id
    left join summary_counts sc on public._gejast_norm_name(sc.player_name) = public._gejast_norm_name(p.display_name)
    left join beerpong_counts bp on public._gejast_norm_name(bp.player_name) = public._gejast_norm_name(p.display_name)
    where nullif(trim(coalesce(p.display_name,'')), '') is not null
  )
  select coalesce(jsonb_agg(jsonb_build_object(
    'player_name', m.display_name,
    'display_name', m.display_name,
    'public_display_name', m.display_name,
    'profile_picture_url', m.profile_picture_url,
    'favorite_game', coalesce(m.favorite_game, case when m.beerpong_matches > greatest(m.klaverjas_matches, m.boerenbridge_matches) then 'Beerpong' when m.boerenbridge_matches > m.klaverjas_matches then 'Boerenbridge' when m.klaverjas_matches > 0 then 'Klaverjas' else null end),
    'total_matches', m.total_matches,
    'total_wins', m.total_wins,
    'best_rating', m.best_rating,
    'best_badge', m.best_badge,
    'klaverjas_matches', m.klaverjas_matches,
    'boerenbridge_matches', m.boerenbridge_matches,
    'beerpong_matches', m.beerpong_matches
  ) order by m.total_matches desc, lower(m.display_name) asc), '[]'::jsonb)
    into v_players
  from merged m;

  return jsonb_build_object('players', v_players);
end;
