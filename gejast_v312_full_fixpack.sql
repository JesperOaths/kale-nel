create or replace function public._gejast_beerpong_player_rows()
returns table(
  match_ref text,
  finished_at timestamptz,
  player_name text,
  is_win boolean,
  participants text[],
  winner_names text[],
  summary_payload jsonb
)
language sql
stable
as $$
  select
    m.client_match_id as match_ref,
    coalesce(m.finished_at, m.created_at) as finished_at,
    p.display_name as player_name,
    case
      when lower(coalesce(m.winner_team,'')) = 'team_a' and p.id = any(m.team_a_player_ids) then true
      when lower(coalesce(m.winner_team,'')) = 'team_b' and p.id = any(m.team_b_player_ids) then true
      else false
    end as is_win,
    array(
      select p2.display_name
      from public.players p2
      where p2.id = any(m.team_a_player_ids) or p2.id = any(m.team_b_player_ids)
      order by case when p2.id = any(m.team_a_player_ids) then array_position(m.team_a_player_ids, p2.id) else 100 + array_position(m.team_b_player_ids, p2.id) end
    ) as participants,
    case
      when lower(coalesce(m.winner_team,'')) = 'team_a' then array(
        select p2.display_name from public.players p2 where p2.id = any(m.team_a_player_ids) order by array_position(m.team_a_player_ids, p2.id)
      )
      when lower(coalesce(m.winner_team,'')) = 'team_b' then array(
        select p2.display_name from public.players p2 where p2.id = any(m.team_b_player_ids) order by array_position(m.team_b_player_ids, p2.id)
      )
      else '{}'::text[]
    end as winner_names,
    jsonb_build_object(
      'match_format', m.match_format,
      'cups_left_team_a', m.cups_left_team_a,
      'cups_left_team_b', m.cups_left_team_b,
      'payload', coalesce(m.payload, '{}'::jsonb)
    ) as summary_payload
  from public.beerpong_matches m
  join public.players p
    on p.id = any(m.team_a_player_ids)
    or p.id = any(m.team_b_player_ids)
  where lower(coalesce(m.match_status,'finished')) = 'finished'
$$;

