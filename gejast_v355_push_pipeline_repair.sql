create or replace function public.queue_nearby_verification_pushes_v2(
  request_kind_input text,
  request_id_input bigint,
  cooldown_seconds integer default 600
) returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  v_kind text := lower(trim(coalesce(request_kind_input,'')));
  v_creator_id bigint;
  v_scope text := 'friends';
  v_lat double precision;
  v_lng double precision;
  v_accuracy double precision;
  v_target_url text;
  v_count integer := 0;
begin
  if v_kind = 'drink' then
    select de.player_id, public._web_push_scope_for_player(de.player_id), de.lat, de.lng, de.accuracy, './drinks_pending.html'
      into v_creator_id, v_scope, v_lat, v_lng, v_accuracy, v_target_url
    from public.drink_events de
    where de.id = request_id_input
      and coalesce(de.status,'pending') = 'pending';
  elsif v_kind = 'speed' then
    select ds.player_id, public._web_push_scope_for_player(ds.player_id), ds.lat, ds.lng, ds.accuracy, './drinks_speed.html'
      into v_creator_id, v_scope, v_lat, v_lng, v_accuracy, v_target_url
    from public.drink_speed_attempts ds
    where ds.id = request_id_input
      and coalesce(ds.status,'pending') = 'pending';
  else
    raise exception 'INVALID_KIND';
  end if;

  if v_creator_id is null then
    return jsonb_build_object('ok', false, 'reason', 'request_not_pending');
  end if;

  insert into public.web_push_jobs(
    status, target_player_id, target_subscription_id, title, body, target_url, payload, site_scope,
    trigger_kind, request_kind, request_id, created_by_player_id, target_player_name, dedupe_key, notification_tag
  )
  select
    'queued',
    s.player_id,
    s.id,
    case when v_kind = 'speed' then 'Nieuwe snelheidspoging in de buurt' else 'Nieuw drankje verifiëren' end,
    case when v_kind = 'speed' then 'Er staat een snelheidspoging in jouw buurt klaar voor verificatie.' else 'Er staat een drankverificatie in jouw buurt klaar.' end,
    v_target_url,
    jsonb_build_object('kind','nearby_verification','request_kind',v_kind,'request_id',request_id_input),
    v_scope,
    'nearby_verification',
    v_kind,
    request_id_input,
    v_creator_id,
    public._web_push_player_name(s.player_id),
    'nearby:' || v_kind || ':' || request_id_input || ':' || s.id,
    'nearby-' || v_kind || '-' || request_id_input
  from public.web_push_subscriptions s
  join public.web_push_active_presence p
    on p.subscription_id = s.id
  where s.disabled_at is null
    and coalesce(s.permission_state,'') = 'granted'
    and p.last_seen_at >= now() - interval '10 minutes'
    and coalesce(p.site_scope, s.site_scope, v_scope) = v_scope
    and s.player_id <> v_creator_id
    and p.lat is not null and p.lng is not null
    and public._web_push_distance_meters(v_lat, v_lng, p.lat, p.lng) <= greatest(coalesce(v_accuracy, 150), coalesce(p.accuracy, 150)) + 350
  on conflict (dedupe_key) do nothing;

  get diagnostics v_count = row_count;
  return jsonb_build_object('ok', true, 'queued_count', v_count, 'request_kind', v_kind, 'request_id', request_id_input, 'site_scope', v_scope);
end;
$$;

