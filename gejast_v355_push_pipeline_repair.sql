        'ok', p.id is not null,
        'presence_id', p.id,
        'last_seen_at', p.last_seen_at,
        'page_path', p.page_path,
        'installation_mode', p.installation_mode,
        'subscription_id', p.subscription_id
      )
      from public.web_push_active_presence p
      where p.player_id = v_player_id
        and coalesce(p.site_scope, v_scope) = v_scope
      order by p.last_seen_at desc nulls last, p.id desc
      limit 1
    ),
    'recent_job', (
      select jsonb_build_object(
        'job_id', j.id,
        'status', j.status,
        'trigger_kind', j.trigger_kind,
        'created_at', j.created_at,
        'claimed_at', j.claimed_at,
        'sent_at', j.sent_at,
        'failed_at', j.failed_at,
        'error_stage', j.error_stage,
        'error_code', j.error_code,
        'error_text', j.error_text
      )
      from public.web_push_jobs j
      where j.target_player_id = v_player_id
        and coalesce(j.site_scope, v_scope) = v_scope
      order by j.created_at desc, j.id desc
      limit 1
    )
  );
end;
$$;

create or replace function public.admin_get_web_push_diagnostics_v2(
  admin_session_token text,
  active_minutes integer default 5,
  site_scope_input text default 'friends'
) returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  v_scope text := coalesce(nullif(trim(coalesce(site_scope_input,'')), ''), 'friends');
  v_cutoff timestamptz := now() - make_interval(mins => greatest(coalesce(active_minutes,5),1));
  v_rows jsonb := '[]'::jsonb;
begin
  perform public.admin_check_session(admin_session_token);

  select coalesce(jsonb_agg(to_jsonb(x) order by x.last_seen_at desc nulls last, lower(x.player_name)), '[]'::jsonb)
    into v_rows
  from (
    select
      coalesce(s.display_name, p.display_name, public._web_push_player_name(coalesce(s.player_id, p.player_id))) as player_name,
      coalesce(s.site_scope, p.site_scope, v_scope) as site_scope,
      s.endpoint,
      s.permission_state,
      s.platform,
      s.installation_mode,
      s.disabled_at,
      s.last_sync_at,
      s.last_success_at,
      s.last_error as subscription_error,
      p.last_seen_at,
      p.page_path,
      p.subscription_id,
      case when s.id is null then 'missing_subscription' when s.disabled_at is not null then 'disabled_subscription' else 'subscription_ok' end as subscription_state,
      case when p.id is null then 'missing_presence' when p.last_seen_at < v_cutoff then 'stale_presence' else 'presence_ok' end as presence_state,
      j.status as job_status,
      j.claimed_at,
      j.sent_at,
      j.failed_at,
      j.error_stage,
      j.error_code,
      j.error_text,
      j.trigger_kind
    from public.web_push_subscriptions s
    full outer join public.web_push_active_presence p
      on p.subscription_id = s.id
    left join lateral (
      select *
      from public.web_push_jobs j
      where j.target_subscription_id = s.id
      order by j.created_at desc, j.id desc
      limit 1
    ) j on true
    where coalesce(s.site_scope, p.site_scope, v_scope) = v_scope
  ) x;

  return jsonb_build_object('ok', true, 'site_scope', v_scope, 'rows', v_rows, 'count', jsonb_array_length(v_rows));
end;
$$;

