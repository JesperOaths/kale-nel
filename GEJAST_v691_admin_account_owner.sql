create or replace function public.admin_get_mail_diagnostics(admin_session_token text, site_scope_input text default null)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_counts jsonb;
  v_recent jsonb;
begin
  perform public._gejast_admin_require_json_v691(admin_session_token);
  select jsonb_object_agg(job_status, ct)
  into v_counts
  from (
    select coalesce(job_status, 'unknown') as job_status, count(*) as ct
    from public.outbound_email_jobs
    group by coalesce(job_status, 'unknown')
  ) s;
  select coalesce(jsonb_agg(to_jsonb(x) order by x.created_at desc), '[]'::jsonb)
  into v_recent
  from (
    select id, job_type, job_status, recipient_email, recipient_name, subject, related_claim_request_id, related_player_id, last_error, sent_at, created_at, updated_at
    from public.outbound_email_jobs
    order by created_at desc
    limit 25
  ) x;
  return jsonb_build_object('ok', true, 'site_scope', coalesce(site_scope_input, 'friends'), 'counts', coalesce(v_counts, '{}'::jsonb), 'recent_jobs', v_recent, 'items', v_recent);
end
$fn$;

