create or replace function public.admin_decide_claim_request_action(admin_session_token text, request_id_input text, decision text, decision_reason_input text default null)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_id bigint := nullif(request_id_input, '')::bigint;
  v_decision text := lower(trim(coalesce(decision, '')));
begin
