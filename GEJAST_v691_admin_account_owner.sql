create or replace function public._gejast_random_hex_v691(byte_count integer default 32)
returns text
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_out text;
begin
  if to_regprocedure('extensions.gen_random_bytes(integer)') is not null then
    execute 'select encode(extensions.gen_random_bytes($1), ''hex'')' into v_out using byte_count;
    return v_out;
  elsif to_regprocedure('public.gen_random_bytes(integer)') is not null then
    execute 'select encode(public.gen_random_bytes($1), ''hex'')' into v_out using byte_count;
    return v_out;
  end if;

  return md5(random()::text || clock_timestamp()::text || txid_current()::text)
      || md5(clock_timestamp()::text || random()::text);
end
$fn$;

create or replace function public._gejast_hash_secret_v691(secret_input text)
returns text
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_hash text;
begin
  if nullif(secret_input, '') is null then
    raise exception 'secret_required';
  end if;

  if to_regprocedure('extensions.crypt(text,text)') is not null
     and to_regprocedure('extensions.gen_salt(text)') is not null then
    execute 'select extensions.crypt($1, extensions.gen_salt(''bf''))' into v_hash using secret_input;
    return v_hash;
  elsif to_regprocedure('public.crypt(text,text)') is not null
     and to_regprocedure('public.gen_salt(text)') is not null then
    execute 'select public.crypt($1, public.gen_salt(''bf''))' into v_hash using secret_input;
    return v_hash;
  end if;

  return 'md5:' || md5(secret_input || ':' || current_database());
end
$fn$;

create or replace function public._gejast_secret_matches_v691(secret_input text, stored_hash text)
returns boolean
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_check text;
begin
  if nullif(stored_hash, '') is null then
    return false;
  end if;

  if stored_hash like 'md5:%' then
    return stored_hash = ('md5:' || md5(coalesce(secret_input, '') || ':' || current_database()));
  end if;

  if to_regprocedure('extensions.crypt(text,text)') is not null then
    execute 'select extensions.crypt($1, $2)' into v_check using coalesce(secret_input, ''), stored_hash;
    return v_check = stored_hash;
  elsif to_regprocedure('public.crypt(text,text)') is not null then
    execute 'select public.crypt($1, $2)' into v_check using coalesce(secret_input, ''), stored_hash;
    return v_check = stored_hash;
  end if;

  return false;
end
$fn$;

create or replace function public.admin_bootstrap_user_v691(
  username_input text,
  password_input text,
  otp_code_input text default null
)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_id bigint;
  v_username text := lower(trim(coalesce(username_input, '')));
begin
  if exists (select 1 from public.gejast_admin_users_v691) then
    raise exception 'admin_bootstrap_already_done';
  end if;
  if v_username = '' then raise exception 'admin_username_required'; end if;
  if length(coalesce(password_input, '')) < 10 then raise exception 'admin_password_min_10_chars'; end if;
  if otp_code_input is not null and otp_code_input !~ '^[0-9]{6}$' then raise exception 'admin_otp_code_must_be_6_digits'; end if;

  insert into public.gejast_admin_users_v691(username, password_hash, otp_code_hash)
  values (v_username, public._gejast_hash_secret_v691(password_input), case when otp_code_input is null then null else public._gejast_hash_secret_v691(otp_code_input) end)
  returning id into v_id;

  return jsonb_build_object('ok', true, 'admin_user_id', v_id, 'username', v_username, 'message', 'Admin user created. Log in with this username, password, and the fixed 6-digit admin code if one was set.');
end
$fn$;

create or replace function public._gejast_admin_require_json_v691(admin_session_token text)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_row record;
begin
  select s.token, s.admin_user_id, s.username, s.role, s.expires_at, u.active
  into v_row
  from public.gejast_admin_sessions_v691 s
  join public.gejast_admin_users_v691 u on u.id = s.admin_user_id
  where s.token = nullif(trim(coalesce(admin_session_token, '')), '')
  limit 1;

  if not found then raise exception 'admin_session_invalid'; end if;
  if v_row.expires_at <= now() then raise exception 'admin_session_expired'; end if;
  if not coalesce(v_row.active, false) then raise exception 'admin_user_disabled'; end if;

  update public.gejast_admin_sessions_v691
  set last_seen_at = now()
  where token = v_row.token;

  return jsonb_build_object(
    'ok', true,
    'id', v_row.admin_user_id,
    'admin_user_id', v_row.admin_user_id,
    'username', v_row.username,
    'admin_username', v_row.username,
    'role', v_row.role,
    'admin_session_token', v_row.token,
    'expires_at', v_row.expires_at
  );
end
$fn$;

