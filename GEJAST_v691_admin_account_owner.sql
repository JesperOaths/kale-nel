create or replace function public.admin_login(input_username text, input_password text, input_totp_code text)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_user record;
  v_token text;
  v_expires timestamptz := now() + interval '12 hours';
begin
  select *
  into v_user
  from public.gejast_admin_users_v691
  where lower(username) = lower(trim(coalesce(input_username, '')))
    and active = true
  limit 1;

  if not found or not public._gejast_secret_matches_v691(coalesce(input_password, ''), v_user.password_hash) then
    raise exception 'admin_login_invalid';
  end if;

  if v_user.otp_code_hash is not null then
    if coalesce(input_totp_code, '') !~ '^[0-9]{6}$'
       or not public._gejast_secret_matches_v691(input_totp_code, v_user.otp_code_hash) then
      raise exception 'admin_otp_invalid';
    end if;
  elsif coalesce(input_totp_code, '') !~ '^[0-9]{6}$' then
    raise exception 'admin_otp_required';
  end if;

  v_token := public._gejast_random_hex_v691(32);

  insert into public.gejast_admin_sessions_v691(token, admin_user_id, username, role, expires_at)
  values (v_token, v_user.id, v_user.username, v_user.role, v_expires);

  insert into public.gejast_admin_audit_v691(admin_user_id, username, action, metadata)
  values (v_user.id, v_user.username, 'admin_login', jsonb_build_object('expires_at', v_expires));

  return jsonb_build_object('ok', true, 'admin_session_token', v_token, 'token', v_token, 'admin_username', v_user.username, 'username', v_user.username, 'role', v_user.role, 'expires_at', v_expires);
end
$fn$;

