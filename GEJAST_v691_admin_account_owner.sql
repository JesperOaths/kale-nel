create or replace function public.admin_validate_outbound_email_job(admin_session_token text, job_id_input bigint, mark_failed_input boolean default true)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_payload jsonb;
  v_reasons text[] := array[]::text[];
  v_email text;
  v_subject text;
  v_url text;
begin
  perform public._gejast_admin_require_json_v691(admin_session_token);
  if job_id_input is null then
    return jsonb_build_object('ok', false, 'reasons', jsonb_build_array('missing_job_id'), 'message', 'Mailjob-id ontbreekt.');
  end if;
  v_payload := public._gejast_email_payload_for_job_v691(job_id_input);
  v_email := nullif(trim(coalesce(v_payload->>'to', v_payload->>'email', v_payload->>'recipient_email')), '');
  v_subject := nullif(trim(coalesce(v_payload->>'email_subject', v_payload->>'subject')), '');
  v_url := nullif(trim(coalesce(v_payload->>'activation_url', v_payload->>'reset_url', v_payload->>'url')), '');

  if v_email is null then v_reasons := array_append(v_reasons, 'missing_email');
  elsif v_email !~* '^[^@\s]+@[^@\s]+\.[^@\s]+$' then v_reasons := array_append(v_reasons, 'invalid_email'); end if;
  if v_subject is null then v_reasons := array_append(v_reasons, 'missing_subject'); end if;
  if v_url is null then v_reasons := array_append(v_reasons, 'missing_activation_url');
  elsif v_url !~* '^https?://' then v_reasons := array_append(v_reasons, 'invalid_activation_url'); end if;

  if array_length(v_reasons, 1) is not null and mark_failed_input then
    update public.outbound_email_jobs
    set job_status = 'failed', last_error = array_to_string(v_reasons, ', '), updated_at = now()
    where id = job_id_input;
  end if;

  return jsonb_build_object(
    'ok', array_length(v_reasons, 1) is null,
    'job_id', job_id_input,
    'reasons', coalesce(to_jsonb(v_reasons), '[]'::jsonb),
    'message', case when array_length(v_reasons, 1) is null then 'ok' else 'Mailjob niet naar Make gestuurd: ' || array_to_string(v_reasons, ', ') end,
    'payload', v_payload,
    'make_payload', v_payload
  );
end
