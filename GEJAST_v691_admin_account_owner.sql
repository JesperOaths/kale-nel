create or replace function public.admin_get_expired_activation_queue(admin_session_token text)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_items jsonb;
begin
  perform public._gejast_admin_require_json_v691(admin_session_token);

  select coalesce(jsonb_agg(public._gejast_claim_json_v691(cr.id) order by pal.expires_at asc), '[]'::jsonb)
  into v_items
  from public.claim_requests cr
  join public.player_activation_links pal on pal.claim_request_id = cr.id
  where pal.used_at is null
    and pal.expires_at < now()
    and lower(coalesce(cr.status, '')) in ('approved', 'awaiting_activation', 'pending_activation');

  return jsonb_build_object('ok', true, 'expired_queue', v_items, 'items', v_items);
end
