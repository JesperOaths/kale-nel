create or replace function public._gejast_claim_json_v691(row_id bigint)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  cr record;
  pal record;
begin
  select * into cr from public.claim_requests where id = row_id;
  if not found then return null; end if;

  select * into pal
  from public.player_activation_links
  where claim_request_id = cr.id
  order by created_at desc
  limit 1;

  return jsonb_build_object(
    'id', cr.id,
    'request_id', cr.id,
    'requested_name', coalesce(nullif(cr.requested_name, ''), nullif(cr.desired_name, ''), nullif(cr.display_name, '')),
    'desired_name', coalesce(nullif(cr.desired_name, ''), nullif(cr.requested_name, ''), nullif(cr.display_name, '')),
    'display_name', public._gejast_claim_display_name_v691(cr.id),
    'requester_email', cr.requester_email,
    'email', cr.requester_email,
    'requester_note', cr.requester_note,
    'status', coalesce(cr.status, cr.decision, 'pending'),
    'decision', coalesce(cr.decision, cr.status),
    'decision_reason', cr.decision_reason,
    'created_at', cr.created_at,
    'updated_at', cr.updated_at,
    'decided_at', cr.decided_at,
    'approved_at', cr.approved_at,
    'rejected_at', cr.rejected_at,
    'activated_at', cr.activated_at,
    'expires_at', coalesce(pal.expires_at, cr.expires_at),
    'player_id', cr.player_id,
    'allowed_username_id', cr.allowed_username_id,
    'site_scope', cr.site_scope,
    'activation_token', pal.activation_token,
    'activation_url', pal.activation_url,
    'has_pin', cr.activated_at is not null
  );
end
$fn$;

