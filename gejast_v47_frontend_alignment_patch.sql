-- gejast v47 frontend alignment + queue patch
create extension if not exists pgcrypto;

create table if not exists public.outbound_email_jobs (
  id bigint generated by default as identity primary key,
  job_type text not null,
  job_status text not null default 'queued',
  recipient_email text not null,
  recipient_name text null,
  subject text not null,
  payload jsonb not null default '{}'::jsonb,
  requested_by_admin_id bigint null,
  related_claim_request_id bigint null references public.claim_requests(id) on delete set null,
  related_player_id bigint null references public.players(id) on delete set null,
  last_error text null,
  provider_message_id text null,
  sent_at timestamptz null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists outbound_email_jobs_status_idx on public.outbound_email_jobs(job_status, created_at desc);
create index if not exists outbound_email_jobs_claim_idx on public.outbound_email_jobs(related_claim_request_id);
create index if not exists outbound_email_jobs_email_idx on public.outbound_email_jobs(lower(recipient_email));

create or replace function public._touch_outbound_email_job_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at := now();
  return new;
end;
$$;

drop trigger if exists trg_outbound_email_jobs_updated_at on public.outbound_email_jobs;
create trigger trg_outbound_email_jobs_updated_at
before update on public.outbound_email_jobs
for each row
execute function public._touch_outbound_email_job_updated_at();

create or replace function public.get_gejast_homepage_state(
  session_token text default null
)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  base_state jsonb;
  can_count integer;
begin
  base_state := public.get_public_state(session_token);
  can_count := coalesce((base_state ->> 'can_count')::integer, (base_state ->> 'yes_count')::integer, 0);

  if coalesce(base_state ->> 'status_key', '') <> '' and coalesce(base_state ->> 'status_label', '') <> '' then
    return base_state;
  end if;

  return coalesce(base_state, '{}'::jsonb)
    || jsonb_build_object(
      'status_key',
      case when can_count >= 4 then 'yes' when can_count = 3 then 'maybe' else 'no' end,
      'status_label',
      case when can_count >= 4 then 'Ja' when can_count = 3 then 'Bijna' else 'Nee' end
    );
end;
$$;

comment on function public.get_gejast_homepage_state(text)
is 'Homepage wrapper around get_public_state that guarantees status_key and status_label.';

create or replace function public.admin_queue_activation_email(
  admin_session_token text,
  request_id_input bigint,
  base_url text,
  recipient_email_input text default null,
  recipient_name_input text default null,
  activation_link_input text default null,
  player_id_input bigint default null
)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  admin_row record;
  req public.claim_requests%rowtype;
  activation_payload jsonb;
  activation_url text;
  recipient_email_clean text;
  recipient_name_clean text;
  job_id_out bigint;
begin
  select * into admin_row from public._require_valid_admin_session(admin_session_token);

  select * into req
  from public.claim_requests
  where id = request_id_input
  limit 1;

  if not found then
    raise exception 'Aanvraag niet gevonden';
  end if;

  if lower(coalesce(req.status, '')) <> 'approved' then
    raise exception 'Alleen goedgekeurde aanvragen kunnen in de mailqueue';
  end if;

  activation_url := nullif(trim(coalesce(activation_link_input, '')), '');

  if activation_url is null then
    activation_payload := public.create_player_activation_link(admin_session_token, request_id_input, base_url);
    activation_url := activation_payload ->> 'activation_url';
  else
    activation_payload := jsonb_build_object(
      'request_id', req.id,
      'display_name', coalesce(public._claim_request_display_name(req.id), null),
      'requester_email', coalesce(nullif(trim(recipient_email_input), ''), lower(trim(req.requester_email))),
      'activation_url', activation_url
    );
  end if;

  recipient_email_clean := lower(trim(coalesce(nullif(recipient_email_input, ''), req.requester_email)));
  recipient_name_clean := nullif(trim(coalesce(recipient_name_input, activation_payload ->> 'display_name', public._claim_request_display_name(req.id))), '');

  if recipient_email_clean is null or recipient_email_clean = '' then
    raise exception 'Ontvanger-e-mail ontbreekt';
  end if;

  insert into public.outbound_email_jobs (
    job_type, job_status, recipient_email, recipient_name, subject, payload,
    requested_by_admin_id, related_claim_request_id, related_player_id
  )
  values (
    'activation_email',
    'queued',
    recipient_email_clean,
    recipient_name_clean,
    'Activeer je account',
    jsonb_build_object(
      'request_id', req.id,
      'activation_url', activation_url,
      'display_name', coalesce(recipient_name_clean, activation_payload ->> 'display_name'),
      'requester_email', recipient_email_clean,
      'base_url', regexp_replace(trim(base_url), '/+$', ''),
      'template', 'activation_email_v1'
    ),
    admin_row.id,
    req.id,
    coalesce(player_id_input, req.player_id)
  )
  returning id into job_id_out;

  return jsonb_build_object(
    'queued', true,
    'job_id', job_id_out,
    'request_id', req.id,
    'recipient_email', recipient_email_clean,
    'recipient_name', recipient_name_clean,
    'activation_url', activation_url,
    'delivery_mode', 'outbound_email_jobs'
  );
end;
$$;

comment on function public.admin_queue_activation_email(text,bigint,text,text,text,text,bigint)
is 'Queues an activation email job in outbound_email_jobs and returns the activation_url used by admin.html.';

create or replace view public.outbound_email_jobs_pending as
select *
from public.outbound_email_jobs
where job_status = 'queued'
order by created_at asc;
