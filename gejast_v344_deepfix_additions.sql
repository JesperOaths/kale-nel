-- Safe ballroom write path with explicit row targeting.
create table if not exists public.ballroom_safe_state (id integer primary key default 1 check (id = 1), king_name text, king_avatar_url text, claimed_at timestamptz, updated_at timestamptz not null default now());
create table if not exists public.ballroom_safe_members (player_name text primary key, avatar_url text, approved_at timestamptz not null default now(), updated_at timestamptz not null default now());
create table if not exists public.ballroom_safe_requests (id bigint generated by default as identity primary key, player_name text not null, avatar_url text, requested_at timestamptz not null default now(), status text not null default 'pending', resolved_at timestamptz);
create unique index if not exists ballroom_safe_requests_pending_name_idx on public.ballroom_safe_requests (lower(player_name), status);
create or replace function public._ballroom_safe_token(session_token text default null, session_token_input text default null) returns text language sql immutable as $$ select nullif(trim(coalesce(session_token, session_token_input, '')), '') $$;
create or replace function public._ballroom_safe_require_name(session_token text default null, session_token_input text default null) returns text language plpgsql security definer as $$ declare viewer text := public._gejast_name_for_session(public._ballroom_safe_token(session_token, session_token_input)); begin if viewer is null then raise exception 'Log eerst in als speler.'; end if; return viewer; end; $$;
create or replace function public.get_ballroom_public_state_safe(session_token text default null, session_token_input text default null) returns jsonb language plpgsql security definer as $$ declare viewer_name text := public._gejast_name_for_session(public._ballroom_safe_token(session_token, session_token_input)); king_name text; king_avatar text; approved_members jsonb; pending_requests jsonb; succession_line jsonb; begin select s.king_name, s.king_avatar_url into king_name, king_avatar from public.ballroom_safe_state s where s.id = 1; select coalesce(jsonb_agg(jsonb_build_object('display_name', m.player_name, 'avatar_url', m.avatar_url) order by m.approved_at), '[]'::jsonb) into approved_members from public.ballroom_safe_members m; select coalesce(jsonb_agg(x.obj order by x.approved_at), '[]'::jsonb) into succession_line from (select jsonb_build_object('display_name', m.player_name, 'avatar_url', m.avatar_url) as obj, m.approved_at from public.ballroom_safe_members m where coalesce(lower(m.player_name),'') <> coalesce(lower(king_name),'') order by m.approved_at limit 3) x; select coalesce(jsonb_agg(jsonb_build_object('id', r.id, 'display_name', r.player_name, 'avatar_url', r.avatar_url, 'requested_at', r.requested_at) order by r.requested_at), '[]'::jsonb) into pending_requests from public.ballroom_safe_requests r where r.status='pending'; return jsonb_build_object('has_king', nullif(trim(coalesce(king_name,'')), '') is not null, 'king', case when nullif(trim(coalesce(king_name,'')), '') is not null then jsonb_build_object('display_name', king_name, 'avatar_url', king_avatar) else null end, 'approved_members', approved_members, 'succession_line', succession_line, 'pending_requests', pending_requests, 'viewer', jsonb_build_object('display_name', viewer_name, 'is_king', viewer_name is not null and lower(viewer_name)=lower(coalesce(king_name,'')), 'is_member', viewer_name is not null and exists(select 1 from public.ballroom_safe_members m where lower(m.player_name)=lower(viewer_name)), 'pending', viewer_name is not null and exists(select 1 from public.ballroom_safe_requests r where lower(r.player_name)=lower(viewer_name) and r.status='pending'))); end; $$;
create or replace function public.get_ballroom_state_safe(session_token text default null, session_token_input text default null) returns jsonb language sql security definer as $$ select public.get_ballroom_public_state_safe(session_token, session_token_input) $$;
create or replace function public.ballroom_claim_king_safe(session_token text default null, session_token_input text default null) returns jsonb language plpgsql security definer as $$ declare viewer_name text := public._ballroom_safe_require_name(session_token, session_token_input); current_king text; begin select king_name into current_king from public.ballroom_safe_state where id = 1; if nullif(trim(coalesce(current_king,'')), '') is not null and lower(current_king) <> lower(viewer_name) then raise exception 'Er is al een koning in de balzaal.'; end if; insert into public.ballroom_safe_state(id, king_name, claimed_at, updated_at) values (1, viewer_name, now(), now()) on conflict (id) do update set king_name = excluded.king_name, claimed_at = coalesce(public.ballroom_safe_state.claimed_at, now()), updated_at = now(); insert into public.ballroom_safe_members(player_name, approved_at, updated_at) values (viewer_name, now(), now()) on conflict (player_name) do update set updated_at = now(); return public.get_ballroom_public_state_safe(session_token, session_token_input); end; $$;
create or replace function public.ballroom_request_entry_safe(session_token text default null, session_token_input text default null) returns jsonb language plpgsql security definer as $$ declare viewer_name text := public._ballroom_safe_require_name(session_token, session_token_input); begin if not exists(select 1 from public.ballroom_safe_state where id = 1 and nullif(trim(coalesce(king_name,'')), '') is not null) then raise exception 'Er is nog geen koning in de balzaal.'; end if; if exists(select 1 from public.ballroom_safe_members m where lower(m.player_name)=lower(viewer_name)) then raise exception 'Je staat al in de balzaal.'; end if; if exists(select 1 from public.ballroom_safe_requests r where lower(r.player_name)=lower(viewer_name) and r.status='pending') then raise exception 'Jouw entreeverzoek staat al open.'; end if; insert into public.ballroom_safe_requests(player_name, requested_at, status) values (viewer_name, now(), 'pending'); return public.get_ballroom_public_state_safe(session_token, session_token_input); end; $$;
create or replace function public.ballroom_resolve_request_safe(session_token text default null, session_token_input text default null, request_id_input bigint default null, approve_input boolean default false) returns jsonb language plpgsql security definer as $$ declare viewer_name text := public._ballroom_safe_require_name(session_token, session_token_input); king_name text; req_name text; begin select s.king_name into king_name from public.ballroom_safe_state s where s.id = 1; if lower(coalesce(king_name,'')) <> lower(coalesce(viewer_name,'')) then raise exception 'Alleen de koning mag verzoeken beoordelen.'; end if; if request_id_input is null then raise exception 'Er ontbreekt een verzoek-id.'; end if; select r.player_name into req_name from public.ballroom_safe_requests r where r.id = request_id_input and r.status='pending'; if req_name is null then raise exception 'Dit entreeverzoek is niet meer actief.'; end if; update public.ballroom_safe_requests set status = case when approve_input then 'approved' else 'rejected' end, resolved_at = now() where id = request_id_input and status='pending'; if approve_input then insert into public.ballroom_safe_members(player_name, approved_at, updated_at) values (req_name, now(), now()) on conflict (player_name) do update set updated_at = now(); end if; return public.get_ballroom_public_state_safe(session_token, session_token_input); end; $$;
create or replace function public.ballroom_abdicate_safe(session_token text default null, session_token_input text default null) returns jsonb language plpgsql security definer as $$ declare viewer_name text := public._ballroom_safe_require_name(session_token, session_token_input); current_king text; successor text; begin select king_name into current_king from public.ballroom_safe_state where id = 1; if lower(coalesce(current_king,'')) <> lower(coalesce(viewer_name,'')) then raise exception 'Alleen de koning kan afstand doen van de kroon.'; end if; select m.player_name into successor from public.ballroom_safe_members m where lower(m.player_name) <> lower(viewer_name) order by m.approved_at limit 1; update public.ballroom_safe_state set king_name = successor, claimed_at = case when successor is null then null else now() end, updated_at = now() where id = 1; return public.get_ballroom_public_state_safe(session_token, session_token_input); end; $$;
grant execute on function public.get_ballroom_public_state_safe(text,text) to anon, authenticated;
grant execute on function public.get_ballroom_state_safe(text,text) to anon, authenticated;
grant execute on function public.ballroom_claim_king_safe(text,text) to anon, authenticated;
grant execute on function public.ballroom_request_entry_safe(text,text) to anon, authenticated;
grant execute on function public.ballroom_resolve_request_safe(text,text,bigint,boolean) to anon, authenticated;
