-- Web-push queue state transitions and test queue visibility.
create or replace function public.queue_test_web_push(session_token text)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_player_id bigint := public._gejast_player_id_from_session(session_token);
  v_name text := public._gejast_player_name_from_session(session_token);
  v_count integer := 0;
begin
  if v_player_id is null or v_name is null then
    raise exception 'Log opnieuw in voor testmeldingen.';
  end if;
  insert into public.web_push_jobs(target_player_id, target_subscription_id, title, body, target_url, payload, created_at, updated_at, status)
  select v_player_id, s.id, 'GEJAST testmelding', 'Je toestel is gekoppeld en klaar voor web-push.', './index.html', jsonb_build_object('kind','test','display_name',v_name), now(), now(), 'pending'
  from public.web_push_subscriptions s
  where s.player_id = v_player_id and coalesce(s.disabled_at is null, true);
  get diagnostics v_count = row_count;
  return jsonb_build_object('ok', true, 'queued_count', v_count, 'display_name', v_name);
end;
$fn$;

drop function if exists public.claim_web_push_jobs(integer);
create or replace function public.claim_web_push_jobs(max_jobs integer default 25)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare v_items jsonb := '[]'::jsonb;
begin
  with picked as (
    select j.id
    from public.web_push_jobs j
    join public.web_push_subscriptions s on s.id = j.target_subscription_id
    where j.status = 'pending' and coalesce(s.disabled_at is null, true)
    order by j.created_at asc, j.id asc
    limit greatest(coalesce(max_jobs,25),1)
    for update of j skip locked
  ), claimed as (
    update public.web_push_jobs j
       set status = 'claimed', claimed_at = now(), updated_at = now()
     where j.id in (select id from picked)
     returning j.*
  )
  select coalesce(jsonb_agg(jsonb_build_object('job_id', c.id, 'title', c.title, 'body', c.body, 'target_url', c.target_url, 'endpoint', s.endpoint, 'p256dh_key', s.p256dh_key, 'auth_key', s.auth_key, 'display_name', s.display_name) order by c.created_at asc, c.id asc), '[]'::jsonb)
  into v_items
  from claimed c join public.web_push_subscriptions s on s.id = c.target_subscription_id;
  return jsonb_build_object('items', v_items);
end;
$fn$;

create or replace function public.mark_web_push_job_sent(job_id_input bigint)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
begin
  update public.web_push_jobs set status = 'sent', sent_at = now(), updated_at = now(), error_text = null where id = job_id_input;
  return jsonb_build_object('ok', true, 'job_id', job_id_input);
end;
$fn$;

create or replace function public.mark_web_push_job_failed(job_id_input bigint, error_input text)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare v_sub_id bigint; v_err text := left(coalesce(error_input,''), 1200);
begin
  update public.web_push_jobs set status = 'failed', failed_at = now(), updated_at = now(), error_text = v_err where id = job_id_input returning target_subscription_id into v_sub_id;
  if v_sub_id is not null and (v_err ilike '%410%' or v_err ilike '%404%' or v_err ilike '%unsub%' or v_err ilike '%gone%') then
    update public.web_push_subscriptions set disabled_at = now(), last_error = v_err, updated_at = now() where id = v_sub_id;
  elsif v_sub_id is not null then
    update public.web_push_subscriptions set last_error = v_err, updated_at = now() where id = v_sub_id;
  end if;
  return jsonb_build_object('ok', true, 'job_id', job_id_input);
end;
$fn$;
