-- Live summaries remain the canonical live-state source for homepage LIVE buttons and spectator pages.
create or replace function public.get_live_match_summaries_scoped(session_token text default null, game_type_input text default null, site_scope_input text default 'friends', client_match_id_input text default null)
returns jsonb
language plpgsql
security definer
as $$
declare viewer_name text; scope_norm text := public._gejast_live_scope_norm(site_scope_input); out_matches jsonb;
begin
  viewer_name := public._gejast_name_for_session(session_token);
  if viewer_name is null then raise exception 'Je moet ingelogd zijn om live scoreformulieren te bekijken.'; end if;
  select coalesce(jsonb_agg(jsonb_build_object(
    'game_type', g.game_type,
    'client_match_id', g.client_match_id,
    'site_scope', coalesce(g.site_scope, 'friends'),
    'participants', to_jsonb(coalesce(nullif(g.participant_names, '{}'::text[]), coalesce((select array_agg(value::text) from jsonb_array_elements_text(coalesce(g.summary_payload->'participants','[]'::jsonb))), '{}'::text[]), coalesce((select array_agg(value::text) from jsonb_array_elements_text(coalesce(g.summary_payload->'players','[]'::jsonb))), '{}'::text[]))),
    'winner_names', to_jsonb(coalesce(g.winner_names, '{}'::text[])),
    'submitter_name', coalesce(g.summary_payload->'submitter_meta'->>'submitted_by_name', null),
    'summary_payload', g.summary_payload,
    'created_at', g.created_at,
    'updated_at', coalesce(g.finished_at, g.created_at)
  ) order by coalesce(g.finished_at, g.created_at) desc), '[]'::jsonb)
  into out_matches
  from public.game_match_summaries g
  where (nullif(trim(coalesce(game_type_input,'')), '') is null or lower(g.game_type)=lower(trim(game_type_input)))
    and coalesce(lower(g.site_scope), 'friends') = scope_norm
    and (nullif(trim(coalesce(client_match_id_input,'')), '') is null or g.client_match_id = trim(client_match_id_input))
    and coalesce(g.finished_at, nullif(g.summary_payload->>'finished_at','')::timestamptz) is null;
  return jsonb_build_object('viewer_name', viewer_name, 'site_scope', scope_norm, 'matches', coalesce(out_matches, '[]'::jsonb));
end; $$;
