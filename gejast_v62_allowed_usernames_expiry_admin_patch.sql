execute function public._sync_allowed_username_from_player();

create or replace function public.process_expired_activation_links()
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  v_count integer := 0;
  rec record;
begin
  for rec in
    select pal.id,
           pal.claim_request_id,
           pal.player_id,
           pal.expires_at,
           cr.allowed_username_id
      from public.player_activation_links pal
      join public.claim_requests cr on cr.id = pal.claim_request_id
     where pal.used_at is null
       and pal.expires_at < now()
       and pal.expired_processed_at is null
  loop
    update public.player_activation_links
       set expired_processed_at = now()
     where id = rec.id;

    update public.claim_requests
       set status = 'expired',
           request_status = 'expired'
     where id = rec.claim_request_id;

    update public.allowed_usernames
       set status = 'expired',
           activation_link_id = rec.id,
           updated_at = now()
     where id = rec.allowed_username_id;

    perform public._log_username_event(
      rec.allowed_username_id,
      'activation_expired',
      null,
      rec.claim_request_id,
      jsonb_build_object('activation_link_id', rec.id, 'expires_at', rec.expires_at)
    );

    v_count := v_count + 1;
  end loop;

  return jsonb_build_object('processed_count', v_count);
end;
$$;

create or replace function public.get_requestable_names()
returns jsonb
language sql
security definer
set search_path to 'public'
as $$
  select jsonb_build_object(
    'names', coalesce(
      jsonb_agg(display_name order by lower(display_name)) filter (
        where lower(status) in ('available', 'reserved', 'expired', 'pending_review')
      ),
      '[]'::jsonb
    )
  )
  from public.allowed_usernames;
$$;

create or replace function public.admin_get_allowed_usernames(admin_session_token text)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  sess record;
begin
  select * into sess from public._require_valid_admin_session(admin_session_token);
  if coalesce(sess.ok, false) is not true then
    raise exception 'Ongeldige admin-sessie';
  end if;

  return jsonb_build_object(
    'items', coalesce((
      select jsonb_agg(jsonb_build_object(
        'id', au.id,
        'username', au.username,
        'display_name', au.display_name,
        'status', au.status,
        'reserved_for_email', au.reserved_for_email,
        'player_id', au.player_id,
        'current_claim_request_id', au.current_claim_request_id,
        'updated_at', au.updated_at
      ) order by lower(au.display_name))
      from public.allowed_usernames au
    ), '[]'::jsonb)
  );
end;
$$;

create or replace function public.admin_reserve_allowed_username(
  admin_session_token text,
  display_name_input text,
  reserved_for_email_input text default null,
  reserved_for_person_note_input text default null
)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  sess record;
  v_id bigint;
  v_username text;
begin
  select * into sess from public._require_valid_admin_session(admin_session_token);
  if coalesce(sess.ok, false) is not true then
    raise exception 'Ongeldige admin-sessie';
  end if;

  if trim(coalesce(display_name_input, '')) = '' then
    raise exception 'Display name ontbreekt';
  end if;

  v_username := public._normalize_allowed_username(display_name_input);
  if v_username = '' then
    raise exception 'Kon geen geldige username-slug maken';
  end if;

  insert into public.allowed_usernames (
    username,
    display_name,
    status,
    reserved_for_email,
    reserved_for_person_note,
    approved_by_admin_id
  )
  values (
    v_username,
    trim(display_name_input),
    case when nullif(trim(coalesce(reserved_for_email_input, '')), '') is null then 'available' else 'reserved' end,
    nullif(lower(trim(coalesce(reserved_for_email_input, ''))), ''),
    nullif(trim(coalesce(reserved_for_person_note_input, '')), ''),
    sess.id
  )
  on conflict (username)
  do update set
    display_name = excluded.display_name,
    reserved_for_email = coalesce(excluded.reserved_for_email, public.allowed_usernames.reserved_for_email),
    reserved_for_person_note = coalesce(excluded.reserved_for_person_note, public.allowed_usernames.reserved_for_person_note),
    status = case
      when public.allowed_usernames.status in ('active', 'blocked') then public.allowed_usernames.status
      else excluded.status
    end,
    approved_by_admin_id = sess.id,
    updated_at = now()
  returning id into v_id;

  perform public._log_username_event(
    v_id,
    'username_reserved',
    sess.id,
    null,
    jsonb_build_object('reserved_for_email', nullif(lower(trim(coalesce(reserved_for_email_input, ''))), ''))
  );

  return jsonb_build_object('ok', true, 'allowed_username_id', v_id);
end;
$$;

create or replace function public.admin_get_expired_activation_queue(admin_session_token text)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  sess record;
begin
  select * into sess from public._require_valid_admin_session(admin_session_token);
  if coalesce(sess.ok, false) is not true then
    raise exception 'Ongeldige admin-sessie';
  end if;

  perform public.process_expired_activation_links();

  return jsonb_build_object(
    'items', coalesce((
      select jsonb_agg(jsonb_build_object(
        'request_id', cr.id,
        'display_name', au.display_name,
        'requester_email', cr.requester_email,
        'status', coalesce(cr.request_status, cr.status),
        'expires_at', pal.expires_at,
        'activation_link_id', pal.id
      ) order by pal.expires_at desc)
      from public.claim_requests cr
      join public.allowed_usernames au on au.id = cr.allowed_username_id
      left join public.player_activation_links pal on pal.claim_request_id = cr.id
      where lower(coalesce(cr.request_status, cr.status, '')) = 'expired'
    ), '[]'::jsonb)
  );
end;
$$;

create or replace function public.admin_requeue_expired_activation(
  admin_session_token text,
  request_id_input bigint
)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  sess record;
  v_allowed_username_id bigint;
begin
  select * into sess from public._require_valid_admin_session(admin_session_token);
  if coalesce(sess.ok, false) is not true then
    raise exception 'Ongeldige admin-sessie';
  end if;

  select allowed_username_id into v_allowed_username_id
  from public.claim_requests
  where id = request_id_input;

  if not found then
    raise exception 'Aanvraag niet gevonden';
  end if;

  update public.claim_requests
     set status = 'pending',
         request_status = 'pending_review'
   where id = request_id_input;

  update public.allowed_usernames
     set status = 'pending_review',
         updated_at = now()
   where id = v_allowed_username_id;

  perform public._log_username_event(
    v_allowed_username_id,
    'expired_requeued_for_review',
    sess.id,
    request_id_input,
    '{}'::jsonb
  );

  return jsonb_build_object('ok', true, 'request_id', request_id_input);
end;
$$;

create or replace function public.admin_resend_expired_activation(
  admin_session_token text,
  request_id_input bigint,
  base_url text default 'https://kalenel.nl/activate.html'
)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  sess record;
  v_allowed_username_id bigint;
  v_result jsonb;
begin
  select * into sess from public._require_valid_admin_session(admin_session_token);
  if coalesce(sess.ok, false) is not true then
    raise exception 'Ongeldige admin-sessie';
  end if;

  select allowed_username_id into v_allowed_username_id
  from public.claim_requests
  where id = request_id_input;

  if not found then
    raise exception 'Aanvraag niet gevonden';
  end if;

  update public.claim_requests
     set status = 'approved',
         request_status = 'approved_pending_activation'
   where id = request_id_input;

  update public.allowed_usernames
     set status = 'approved_pending_activation',
         updated_at = now()
   where id = v_allowed_username_id;

  v_result := public.admin_queue_activation_email(
    admin_session_token,
    request_id_input,
    base_url,
    null,
    null,
    null,
    null
  );

  perform public._log_username_event(
    v_allowed_username_id,
    'expired_activation_resent',
    sess.id,
    request_id_input,
    coalesce(v_result, '{}'::jsonb)
  );

  return v_result;
end;
$$;

