          ) as row_json
        from public.beerpong_matches m
        where m.deleted_at is null
          and (v_is_admin or (v_player.id is not null and m.created_by_player_id = v_player.id))
        order by m.created_at desc
        limit v_limit
      ) s;
  elsif v_game = 'boerenbridge' then
    select coalesce(jsonb_agg(row_json order by created_at desc), '[]'::jsonb)
      into v_rows
      from (
        select m.created_at,
          jsonb_build_object(
            'client_match_id', m.client_match_id,
            'created_at', m.created_at,
            'updated_at', m.updated_at,
            'finished_at', m.finished_at,
            'payload', coalesce(m.payload, '{}'::jsonb),
            'participant_names', coalesce((select jsonb_agg(coalesce(p->>'name', p->>'display_name', p->>'player_name', p::text) order by ord) from jsonb_array_elements(coalesce(m.payload->'players', m.payload->'participants', m.payload#>'{raw_payload,players}', '[]'::jsonb)) with ordinality as t(p, ord)), '[]'::jsonb),
            'winner_names', coalesce(m.payload#>'{match_summary,winner_names}', m.payload->'winner_names', '[]'::jsonb),
            'can_edit', (v_is_admin or (v_player.id is not null and m.created_by_player_id = v_player.id and now() <= m.created_at + interval '5 minutes')),
            'can_delete', (v_is_admin or (v_player.id is not null and m.created_by_player_id = v_player.id and now() <= m.created_at + interval '5 minutes'))
          ) as row_json
        from public.boerenbridge_matches m
        where m.deleted_at is null
          and (v_is_admin or (v_player.id is not null and m.created_by_player_id = v_player.id))
        order by m.created_at desc
        limit v_limit
      ) s;
  elsif v_game = 'klaverjas' then
    select coalesce(jsonb_agg(row_json order by created_at desc), '[]'::jsonb)
      into v_rows
      from (
        select j.created_at,
          jsonb_build_object(
            'client_match_id', j.id::text,
            'created_at', j.created_at,
            'updated_at', j.updated_at,
            'finished_at', j.created_at,
            'payload', coalesce(j.payload, '{}'::jsonb),
            'participant_names', coalesce((select jsonb_agg(e.display_name order by e.seat_no) from public.jas_game_entries e where e.game_id = j.id), '[]'::jsonb),
            'winner_names', coalesce((select jsonb_agg(e.display_name order by e.seat_no) from public.jas_game_entries e where e.game_id = j.id and coalesce(e.is_winner, false) = true), '[]'::jsonb),
