          ) as row_json
        from public.jas_games j
        where j.deleted_at is null
          and (v_is_admin or (v_player.id is not null and j.created_by_player_id = v_player.id))
        order by j.created_at desc
        limit v_limit
      ) s;
  else
    raise exception 'Onbekend speltype: %', v_game;
  end if;

  return jsonb_build_object(v_game, coalesce(v_rows,'[]'::jsonb));
end;
