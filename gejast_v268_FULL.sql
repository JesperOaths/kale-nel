          nullif(trim(coalesce(to_jsonb(p)->>'ghost_note','')), '') as ghost_note,
          case
            when jsonb_typeof(coalesce(to_jsonb(p)->'ghost_original_names', '[]'::jsonb)) = 'array'
              then coalesce(to_jsonb(p)->'ghost_original_names', '[]'::jsonb)
            else '[]'::jsonb
          end as ghost_original_names,
          coalesce(nullif(coalesce(to_jsonb(p)->>'updated_at',''), '')::timestamptz,
                   nullif(coalesce(to_jsonb(p)->>'created_at',''), '')::timestamptz,
                   now()) as updated_at
        from public.players p
      ),
      allowed_rows as (
        select
          'allowed_username'::text as source_kind,
          au.id::text as source_key,
          au.id,
          coalesce(au.username, public._normalize_allowed_username(au.display_name)) as username,
          au.display_name,
          au.status,
          au.reserved_for_email,
          au.reserved_for_person_note,
          au.player_id,
          au.current_claim_request_id,
          au.activation_link_id,
          au.blocked_reason,
          coalesce(au.updated_at, now()) as updated_at,
          ps.display_name as player_display_name,
          coalesce(ps.hidden_from_public, false) as player_hidden_from_public,
          coalesce(ps.is_dummy, false) as player_is_dummy,
          ps.ghosted_at as player_ghosted_at,
          ps.ghost_note as player_ghost_note,
          coalesce(ps.ghost_original_names, '[]'::jsonb) as player_ghost_original_names,
          false as is_virtual
        from public.allowed_usernames au
        left join player_source ps on ps.player_id = au.player_id
      ),
      player_only_rows as (
        select
          'player_only'::text as source_kind,
          ('player:' || ps.player_id::text)::text as source_key,
          null::bigint as id,
          ps.username,
          ps.display_name,
          case when ps.hidden_from_public or ps.is_dummy or ps.ghosted_at is not null then 'ghosted-player' else 'linked-player' end as status,
          null::text as reserved_for_email,
          'Speler zonder losse kieslijstregel'::text as reserved_for_person_note,
          ps.player_id,
          null::bigint as current_claim_request_id,
          null::bigint as activation_link_id,
          null::text as blocked_reason,
          ps.updated_at,
          ps.display_name as player_display_name,
          ps.hidden_from_public as player_hidden_from_public,
          ps.is_dummy as player_is_dummy,
          ps.ghosted_at as player_ghosted_at,
          ps.ghost_note as player_ghost_note,
          ps.ghost_original_names as player_ghost_original_names,
          true as is_virtual
        from player_source ps
        where nullif(trim(coalesce(ps.display_name,'')), '') is not null
          and not exists (
            select 1 from public.allowed_usernames au
            where au.player_id = ps.player_id
               or lower(trim(coalesce(au.display_name,''))) = ps.display_name_norm
               or lower(trim(coalesce(au.username,''))) = lower(trim(coalesce(ps.username,'')))
          )
      ),
      ghost_name_rows as (
        select
          'archive_name'::text as source_kind,
          ('archive:' || ps.player_id::text || ':' || lower(trim(g.value)))::text as source_key,
          null::bigint as id,
          public._normalize_allowed_username(g.value) as username,
          g.value as display_name,
          'ghosted-name'::text as status,
          null::text as reserved_for_email,
          coalesce(ps.ghost_note, 'Verborgen archiefnaam') as reserved_for_person_note,
          ps.player_id,
          null::bigint as current_claim_request_id,
          null::bigint as activation_link_id,
          null::text as blocked_reason,
          coalesce(ps.ghosted_at, ps.updated_at, now()) as updated_at,
          ps.display_name as player_display_name,
          ps.hidden_from_public as player_hidden_from_public,
          ps.is_dummy as player_is_dummy,
          ps.ghosted_at as player_ghosted_at,
          ps.ghost_note as player_ghost_note,
          ps.ghost_original_names as player_ghost_original_names,
          true as is_virtual
        from player_source ps
        cross join lateral jsonb_array_elements_text(ps.ghost_original_names) g(value)
        where nullif(trim(g.value), '') is not null
          and not exists (
            select 1 from public.allowed_usernames au
            where lower(trim(coalesce(au.display_name,''))) = lower(trim(g.value))
               or lower(trim(coalesce(au.username,''))) = lower(public._normalize_allowed_username(g.value))
          )
      ),
      merged as (
        select * from allowed_rows
        union all
        select * from player_only_rows
        union all
        select * from ghost_name_rows
      )
      select jsonb_agg(
        jsonb_build_object(
          'source_kind', m.source_kind,
          'source_key', m.source_key,
          'id', m.id,
          'username', m.username,
          'display_name', m.display_name,
          'status', m.status,
          'reserved_for_email', m.reserved_for_email,
          'reserved_for_person_note', m.reserved_for_person_note,
          'player_id', m.player_id,
          'current_claim_request_id', m.current_claim_request_id,
          'activation_link_id', m.activation_link_id,
          'blocked_reason', m.blocked_reason,
          'updated_at', m.updated_at,
          'player_display_name', m.player_display_name,
          'player_hidden_from_public', m.player_hidden_from_public,
          'player_is_dummy', m.player_is_dummy,
          'player_ghosted_at', m.player_ghosted_at,
          'player_ghost_note', m.player_ghost_note,
          'player_ghost_original_names', m.player_ghost_original_names,
          'is_virtual', m.is_virtual
        )
        order by lower(trim(coalesce(m.display_name,''))), m.updated_at desc, m.source_key
      )
      from merged m
    ), '[]'::jsonb)
  );
end;
