-- Generic hidden-name feed for public pages if they need a cheap front-end filter.
create or replace function public.get_public_ghost_filter()
returns jsonb
language sql
security definer
set search_path to 'public'
as $$
  with hidden_rows as (
    select p.id,
           p.display_name,
           p.ghost_original_names
      from public.players p
     where coalesce(p.hidden_from_public, false) is true
        or coalesce(p.is_dummy, false) is true
  ), names as (
    select lower(trim(display_name)) as hidden_name
      from hidden_rows
     where nullif(trim(coalesce(display_name,'')), '') is not null
    union
    select lower(trim(value::text)) as hidden_name
      from hidden_rows,
           lateral jsonb_array_elements_text(case when jsonb_typeof(ghost_original_names) = 'array' then ghost_original_names else '[]'::jsonb end) value
     where nullif(trim(coalesce(value::text,'')), '') is not null
  )
  select jsonb_build_object(
    'hidden_names', coalesce((select jsonb_agg(hidden_name order by hidden_name) from names), '[]'::jsonb)
  );
$$;

-- Helper for the later approved hidden-ghost model.
drop function if exists public._mark_player_hidden(bigint, boolean, boolean, text);
create or replace function public._mark_player_hidden(
  p_player_id bigint,
  p_hidden boolean default true,
  p_is_dummy boolean default true,
  p_reason text default null
) returns public.players
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_player public.players%rowtype;
begin
  update public.players
     set hidden_from_public = coalesce(p_hidden, true),
         is_dummy = case when p_hidden then coalesce(p_is_dummy, true) else is_dummy end,
         ghosted_at = case when p_hidden then now() else null end,
         ghost_note = case when p_hidden then nullif(trim(coalesce(p_reason,'')), '') else null end
   where id = p_player_id
   returning * into v_player;

  if v_player.id is null then
    raise exception 'Speler niet gevonden.';
  end if;

  return v_player;
end;
$fn$;

create or replace function public.admin_set_player_ghost_status(
  admin_session_token text,
  target_player_id bigint default null,
  target_display_name text default null,
  ghosted boolean default true,
  reason_input text default null
) returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_admin jsonb;
  v_admin_name text;
  v_player public.players%rowtype;
  v_original_display_name text;
  v_original_username text;
  v_alias_display_name text;
  v_alias_username text;
  v_reason text := nullif(trim(coalesce(reason_input,'')), '');
  v_sessions_cleared integer := 0;
  v_has_username boolean := false;
  v_has_active boolean := false;
  v_has_pin_hash boolean := false;
  v_has_approved boolean := false;
  v_has_approved_email boolean := false;
  v_has_email boolean := false;
  v_has_updated_at boolean := false;
  v_allowed_id bigint;
  v_now timestamptz := now();
begin
  v_admin := to_jsonb(public.admin_check_session(admin_session_token));
  if coalesce((v_admin->>'ok')::boolean, false) is not true then
    raise exception 'Log opnieuw in als admin.';
  end if;

  v_admin_name := coalesce(nullif(trim(v_admin->>'admin_username'),''), nullif(trim(v_admin->>'username'),''), 'admin');

  if target_player_id is not null then
    select * into v_player from public.players where id = target_player_id;
  elsif nullif(trim(coalesce(target_display_name,'')), '') is not null then
    select p.*
      into v_player
      from public.players p
     where lower(trim(p.display_name)) = lower(trim(target_display_name))
        or exists (
             select 1
               from jsonb_array_elements_text(
                 case
                   when jsonb_typeof(coalesce(p.ghost_original_names, '[]'::jsonb)) = 'array'
                     then coalesce(p.ghost_original_names, '[]'::jsonb)
                   else '[]'::jsonb
                 end
               ) v
              where lower(trim(v::text)) = lower(trim(target_display_name))
           )
        or exists (
             select 1
               from public.allowed_usernames au
              where au.player_id = p.id
                and lower(trim(au.display_name)) = lower(trim(target_display_name))
           )
     order by
       case when lower(trim(p.display_name)) = lower(trim(target_display_name)) then 0 else 1 end,
       p.id desc
     limit 1;
  else
    raise exception 'Geef een target_player_id of target_display_name mee.';
  end if;

  if v_player.id is null then
    raise exception 'Speler niet gevonden.';
  end if;

  v_original_display_name := nullif(trim(coalesce(v_player.display_name,'')), '');

  select exists (
    select 1 from information_schema.columns
     where table_schema = 'public' and table_name = 'players' and column_name = 'username'
  ) into v_has_username;
  select exists (
    select 1 from information_schema.columns
     where table_schema = 'public' and table_name = 'players' and column_name = 'active'
  ) into v_has_active;
  select exists (
    select 1 from information_schema.columns
     where table_schema = 'public' and table_name = 'players' and column_name = 'pin_hash'
  ) into v_has_pin_hash;
  select exists (
    select 1 from information_schema.columns
     where table_schema = 'public' and table_name = 'players' and column_name = 'approved'
  ) into v_has_approved;
  select exists (
    select 1 from information_schema.columns
     where table_schema = 'public' and table_name = 'players' and column_name = 'approved_email'
  ) into v_has_approved_email;
  select exists (
    select 1 from information_schema.columns
     where table_schema = 'public' and table_name = 'players' and column_name = 'email'
  ) into v_has_email;
  select exists (
    select 1 from information_schema.columns
     where table_schema = 'public' and table_name = 'players' and column_name = 'updated_at'
  ) into v_has_updated_at;

  if v_has_username then
    execute $$select nullif(trim(coalesce(username, '')), '') from public.players where id = $1$$ into v_original_username using v_player.id;
  end if;

  if ghosted then
    perform public._mark_player_hidden(v_player.id, true, true, v_reason);

    update public.players
       set ghost_original_names = (
             select coalesce(jsonb_agg(distinct entry), '[]'::jsonb)
               from (
                 select value::text as entry
                   from jsonb_array_elements_text(case when jsonb_typeof(coalesce(public.players.ghost_original_names, '[]'::jsonb)) = 'array' then coalesce(public.players.ghost_original_names, '[]'::jsonb) else '[]'::jsonb end)
                 union all
                 select v_original_display_name
               ) s
              where nullif(trim(coalesce(entry,'')), '') is not null
           )
     where id = v_player.id
       and v_original_display_name is not null;

    v_alias_display_name := '__ghost_' || v_player.id::text;
    v_alias_username := public._normalize_allowed_username(v_alias_display_name);

    execute 'update public.players set display_name = $2' ||
            case when v_has_username then ', username = $3' else '' end ||
            case when v_has_active then ', active = false' else '' end ||
            case when v_has_pin_hash then ', pin_hash = null' else '' end ||
            case when v_has_approved then ', approved = false' else '' end ||
            case when v_has_approved_email then ', approved_email = null' else '' end ||
            case when v_has_email then ', email = null' else '' end ||
            case when v_has_updated_at then ', updated_at = now()' else '' end ||
            ' where id = $1'
      using v_player.id, v_alias_display_name, v_alias_username;

    if exists (
      select 1
        from information_schema.columns
       where table_schema = 'public'
         and table_name = 'sessions'
         and column_name = 'player_id'
    ) then
      execute 'delete from public.sessions where player_id = $1' using v_player.id;
      get diagnostics v_sessions_cleared = row_count;
    end if;

    if to_regclass('public.allowed_usernames') is not null and v_original_display_name is not null then
      select id into v_allowed_id
        from public.allowed_usernames
       where lower(display_name) = lower(v_original_display_name)
       order by id desc
       limit 1;

      if v_allowed_id is null then
        insert into public.allowed_usernames (
          username,
          display_name,
          status,
          reserved_for_email,
          reserved_for_person_note,
          player_id,
          current_claim_request_id,
          activation_link_id,
          blocked_reason,
          updated_at
        )
        values (
          public._normalize_allowed_username(v_original_display_name),
          v_original_display_name,
          'available',
          null,
          null,
          null,
          null,
          null,
          null,
          now()
        )
        returning id into v_allowed_id;
      else
        update public.allowed_usernames
           set username = public._normalize_allowed_username(v_original_display_name),
               display_name = v_original_display_name,
               status = 'available',
               reserved_for_email = null,
               reserved_for_person_note = null,
               player_id = null,
               current_claim_request_id = null,
               activation_link_id = null,
               blocked_reason = null,
               updated_at = now()
         where id = v_allowed_id;
      end if;
    end if;
  else
    perform public._mark_player_hidden(v_player.id, false, coalesce(v_player.is_dummy, true), v_reason);
  end if;

  insert into public.match_change_log(
    game_type,
    game_key,
    match_ref,
    client_match_id,
    change_type,
    changed_by_role,
    changed_by_name,
    reason,
    before_payload,
    after_payload
  )
  values (
    'admin',
    'admin',
    'player:' || v_player.id::text,
    'player:' || v_player.id::text,
    case when ghosted then 'ghost_player' else 'unghost_player' end,
    'admin',
    v_admin_name,
    v_reason,
    jsonb_build_object(
      'player_id', v_player.id,
      'display_name', v_player.display_name,
      'hidden_from_public', v_player.hidden_from_public,
      'is_dummy', v_player.is_dummy,
      'ghosted_at', v_player.ghosted_at,
      'ghost_note', v_player.ghost_note,
      'ghost_original_names', v_player.ghost_original_names,
      'original_display_name', v_original_display_name,
      'original_username', v_original_username
    ),
    (
      select jsonb_build_object(
        'player_id', p.id,
        'display_name', p.display_name,
        'hidden_from_public', p.hidden_from_public,
        'is_dummy', p.is_dummy,
        'ghosted_at', p.ghosted_at,
        'ghost_note', p.ghost_note,
        'ghost_original_names', p.ghost_original_names,
        'sessions_cleared', v_sessions_cleared,
        'allowed_name_released', v_original_display_name
      )
      from public.players p
      where p.id = v_player.id
    )
  );

  return (
    select jsonb_build_object(
      'ok', true,
      'player_id', p.id,
      'display_name', p.display_name,
      'hidden_from_public', p.hidden_from_public,
      'is_dummy', p.is_dummy,
      'ghosted_at', p.ghosted_at,
      'ghost_note', p.ghost_note,
      'ghost_original_names', p.ghost_original_names,
      'sessions_cleared', v_sessions_cleared,
      'released_display_name', v_original_display_name
    )
    from public.players p
    where p.id = v_player.id
  );
end;
$fn$;

-- Extend admin allowed names payload so the admin page can manage hidden archive rows.

create or replace function public.admin_get_allowed_usernames(admin_session_token text)
returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  sess record;
begin
  if to_regprocedure('public._require_valid_admin_session(text)') is not null then
    execute 'select * from public._require_valid_admin_session($1)' into sess using admin_session_token;
    if coalesce(sess.ok, false) is not true then
      raise exception 'Ongeldige admin-sessie';
    end if;
  else
    perform public.admin_check_session(admin_session_token);
  end if;

  return jsonb_build_object(
    'items',
    coalesce((
      with allowed_rows as (
        select
          'allowed_username'::text as source_kind,
          au.id::text as source_key,
          au.id,
          au.username,
          au.display_name,
          au.status,
          au.reserved_for_email,
          au.reserved_for_person_note,
          au.player_id,
          au.current_claim_request_id,
          au.activation_link_id,
          au.blocked_reason,
          au.updated_at,
          p.display_name as player_display_name,
          coalesce(p.hidden_from_public, false) as player_hidden_from_public,
          coalesce(p.is_dummy, false) as player_is_dummy,
          p.ghosted_at as player_ghosted_at,
          p.ghost_note as player_ghost_note,
          coalesce(p.ghost_original_names, '[]'::jsonb) as player_ghost_original_names,
          false as is_virtual
        from public.allowed_usernames au
        left join public.players p on p.id = au.player_id
      ),
      player_only_rows as (
        select
          'player_only'::text as source_kind,
          ('player:' || p.id::text)::text as source_key,
          null::bigint as id,
          coalesce(nullif(trim(coalesce(to_jsonb(p)->>'username','')), ''), public._normalize_allowed_username(p.display_name)) as username,
          p.display_name,
          case
            when coalesce(p.hidden_from_public, false) or coalesce(p.is_dummy, false) or p.ghosted_at is not null then 'ghosted-player'
            else 'linked-player'
          end as status,
          null::text as reserved_for_email,
          'Speler zonder losse kieslijstregel'::text as reserved_for_person_note,
          p.id as player_id,
          null::bigint as current_claim_request_id,
          null::bigint as activation_link_id,
          null::text as blocked_reason,
          coalesce(p.updated_at, p.created_at, now()) as updated_at,
          p.display_name as player_display_name,
          coalesce(p.hidden_from_public, false) as player_hidden_from_public,
          coalesce(p.is_dummy, false) as player_is_dummy,
          p.ghosted_at as player_ghosted_at,
          p.ghost_note as player_ghost_note,
          coalesce(p.ghost_original_names, '[]'::jsonb) as player_ghost_original_names,
          true as is_virtual
        from public.players p
        where nullif(trim(coalesce(p.display_name, '')), '') is not null
          and not exists (
            select 1
            from public.allowed_usernames au
            where au.player_id = p.id
               or lower(trim(coalesce(au.display_name, ''))) = lower(trim(coalesce(p.display_name, '')))
          )
      ),
      ghost_name_candidates as (
        select
          p.id as player_id,
          candidate_name
        from public.players p
        cross join lateral (
          select distinct nullif(trim(candidate), '') as candidate_name
          from (
            select p.display_name as candidate
            union all
            select value::text as candidate
            from jsonb_array_elements_text(
              case
                when jsonb_typeof(coalesce(p.ghost_original_names, '[]'::jsonb)) = 'array'
                  then coalesce(p.ghost_original_names, '[]'::jsonb)
                else '[]'::jsonb
              end
            )
          ) names
        ) n
        where p.hidden_from_public is true
           or p.is_dummy is true
           or p.ghosted_at is not null
      ),
      orphan_archive_rows as (
        select
          'player_archive'::text as source_kind,
          ('player:' || p.id::text || ':' || public._normalize_allowed_username(gc.candidate_name))::text as source_key,
          null::bigint as id,
          public._normalize_allowed_username(gc.candidate_name) as username,
          gc.candidate_name as display_name,
          case
            when exists (
              select 1 from public.allowed_usernames au2
              where lower(trim(au2.display_name)) = lower(trim(gc.candidate_name))
            ) then 'available'
            else 'archive-only'
          end as status,
          null::text as reserved_for_email,
          'Verborgen spelerarchief'::text as reserved_for_person_note,
          p.id as player_id,
          null::bigint as current_claim_request_id,
          null::bigint as activation_link_id,
          'hidden archive'::text as blocked_reason,
          coalesce(p.ghosted_at, p.updated_at, p.created_at, now()) as updated_at,
          p.display_name as player_display_name,
          coalesce(p.hidden_from_public, false) as player_hidden_from_public,
          coalesce(p.is_dummy, false) as player_is_dummy,
          p.ghosted_at as player_ghosted_at,
          p.ghost_note as player_ghost_note,
          coalesce(p.ghost_original_names, '[]'::jsonb) as player_ghost_original_names,
          true as is_virtual
        from public.players p
        join ghost_name_candidates gc on gc.player_id = p.id
        where not exists (
          select 1
          from public.allowed_usernames au
          where lower(trim(au.display_name)) = lower(trim(gc.candidate_name))
             or (au.player_id = p.id and lower(trim(au.display_name)) = lower(trim(p.display_name)))
        )
      )
      select jsonb_agg(
