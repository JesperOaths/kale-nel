                then coalesce(to_jsonb(p)->'ghost_original_names','[]'::jsonb)
                else '[]'::jsonb end
         ) g(value)
         where lower(trim(g.value)) = v_norm
       )
    )
    and coalesce((to_jsonb(p)->>'hidden_from_public')::boolean, false) is false
    and coalesce((to_jsonb(p)->>'is_dummy')::boolean, false) is false
    and nullif(coalesce(to_jsonb(p)->>'ghosted_at',''), '') is null
