                then coalesce(to_jsonb(p)->'ghost_original_names','[]'::jsonb)
                else '[]'::jsonb end
         ) g(value)
         where lower(trim(g.value)) = v_norm
       )
  ) into v_has_player;

  select exists(
    select 1
    from public.players p
    where (
       lower(trim(coalesce(p.display_name,''))) = v_norm
       or lower(trim(coalesce(to_jsonb(p)->>'username',''))) = v_norm
