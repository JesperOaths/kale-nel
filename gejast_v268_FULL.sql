create or replace function public._is_public_site_name(p_name text)
returns boolean
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_norm text := lower(trim(coalesce(p_name,'')));
  v_has_player boolean := false;
  v_has_visible boolean := false;
  v_hidden_archive boolean := false;
begin
  if v_norm = '' then
    return false;
  end if;

  select exists (
    select 1
      from public.players p
     where lower(trim(coalesce(p.display_name,''))) = v_norm
  ) into v_has_player;

  select exists (
    select 1
      from public.players p
     where lower(trim(coalesce(p.display_name,''))) = v_norm
       and coalesce((to_jsonb(p)->>'hidden_from_public')::boolean, false) is false
       and coalesce((to_jsonb(p)->>'is_dummy')::boolean, false) is false
       and nullif(coalesce(to_jsonb(p)->>'ghosted_at',''), '') is null
  ) into v_has_visible;

  select exists (
    select 1
      from public.players p
           cross join lateral jsonb_array_elements_text(
             case
               when jsonb_typeof(coalesce(to_jsonb(p)->'ghost_original_names', '[]'::jsonb)) = 'array'
                 then coalesce(to_jsonb(p)->'ghost_original_names', '[]'::jsonb)
               else '[]'::jsonb
             end
           ) g(value)
     where lower(trim(coalesce(g.value,''))) = v_norm
       and (
         coalesce((to_jsonb(p)->>'hidden_from_public')::boolean, false) is true
         or coalesce((to_jsonb(p)->>'is_dummy')::boolean, false) is true
         or nullif(coalesce(to_jsonb(p)->>'ghosted_at',''), '') is not null
       )
  ) into v_hidden_archive;

  if v_hidden_archive then
    return false;
  end if;

  if v_has_player then
    return v_has_visible;
  end if;

  if v_norm ~ '(dummy|__ghost|ghost|testuser|verifier\s*1111|verifier|spookinoza)' then
    return false;
  end if;

  return true;
end;
$fn$;

grant execute on function public._is_public_site_name(text) to anon, authenticated;

create or replace function public._resolve_match_control_ref(
  game_key_input text,
  match_ref_input text
) returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_game text := lower(trim(coalesce(game_key_input,'')));
  v_ref text := trim(coalesce(match_ref_input,''));
  v_id bigint := null;
  v_client text := null;
  v_exists boolean := false;
begin
  if v_game = '' or v_ref = '' then
    raise exception 'Match-ref ontbreekt.';
  end if;

  if v_game = 'klaverjas' then
    if v_ref !~ '^\d+$' then
      raise exception 'Ongeldige klaverjas-ref.';
    end if;
    return jsonb_build_object('game_key', v_game, 'internal_id', v_ref::bigint, 'match_ref', v_ref, 'client_match_id', v_ref, 'legacy', false);
  end if;

  if v_ref like '__legacy__%' then
    v_id := nullif(replace(v_ref, '__legacy__', ''), '')::bigint;
  elsif v_ref ~ '^\d+$' then
    v_id := v_ref::bigint;
  end if;

  if v_game = 'beerpong' then
    if v_id is not null then
      select exists(select 1 from public.beerpong_matches m where m.id = v_id), nullif(trim(coalesce(m.client_match_id,'')), '')
        into v_exists, v_client
        from public.beerpong_matches m
       where m.id = v_id
       limit 1;
    else
      select m.id, nullif(trim(coalesce(m.client_match_id,'')), ''), true
        into v_id, v_client, v_exists
        from public.beerpong_matches m
       where m.client_match_id = v_ref
       limit 1;
    end if;
  elsif v_game = 'boerenbridge' then
    if v_id is not null then
      select exists(select 1 from public.boerenbridge_matches m where m.id = v_id), nullif(trim(coalesce(m.client_match_id,'')), '')
        into v_exists, v_client
        from public.boerenbridge_matches m
       where m.id = v_id
       limit 1;
    else
      select m.id, nullif(trim(coalesce(m.client_match_id,'')), ''), true
        into v_id, v_client, v_exists
        from public.boerenbridge_matches m
       where m.client_match_id = v_ref
       limit 1;
    end if;
  else
    raise exception 'Onbekend speltype: %', v_game;
  end if;

  if not coalesce(v_exists, false) or v_id is null then
    raise exception 'Wedstrijd niet gevonden.';
  end if;

  return jsonb_build_object(
    'game_key', v_game,
    'internal_id', v_id,
    'match_ref', coalesce(v_client, '__legacy__' || v_id::text),
    'client_match_id', v_client,
    'legacy', v_client is null
  );
end;
$fn$;

grant execute on function public._resolve_match_control_ref(text, text) to anon, authenticated;

create or replace function public.get_match_control_rows(
  game_key text,
  session_token text default null,
  admin_session_token text default null,
  limit_count integer default 80
) returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_game text := lower(trim(coalesce(game_key,'')));
  v_limit integer := greatest(coalesce(limit_count, 80), 1);
  v_player public.players%rowtype;
  v_admin jsonb;
  v_rows jsonb := '[]'::jsonb;
begin
  begin
    if nullif(trim(coalesce(session_token,'')), '') is not null then
      select * into v_player from public._gejast_player_from_session(session_token);
    end if;
  exception when others then
    null;
  end;

  begin
    if nullif(trim(coalesce(admin_session_token,'')), '') is not null then
      v_admin := to_jsonb(public.admin_check_session(admin_session_token));
      if coalesce((v_admin->>'ok')::boolean, false) is not true then
        v_admin := null;
      end if;
    end if;
  exception when others then
    v_admin := null;
  end;

  if v_game = 'beerpong' then
    select coalesce(jsonb_agg(row_json order by created_at desc), '[]'::jsonb)
      into v_rows
      from (
        select
          m.created_at,
          jsonb_build_object(
            'client_match_id', coalesce(nullif(trim(coalesce(m.client_match_id,'')), ''), '__legacy__' || m.id::text),
            'raw_client_match_id', nullif(trim(coalesce(m.client_match_id,'')), ''),
            'internal_id', m.id,
            'created_at', m.created_at,
            'updated_at', m.updated_at,
            'finished_at', m.finished_at,
            'payload', coalesce(m.payload, '{}'::jsonb),
            'player_names', array_to_json(coalesce(m.team_a_player_names, '{}'::text[]) || coalesce(m.team_b_player_names, '{}'::text[])),
            'winner_names', array_to_json(
              case
                when lower(coalesce(m.winner_team,'')) in ('team_a','a','team a') then coalesce(m.team_a_player_names, '{}'::text[])
                when lower(coalesce(m.winner_team,'')) in ('team_b','b','team b') then coalesce(m.team_b_player_names, '{}'::text[])
                else '{}'::text[]
              end
            ),
            'can_edit', ((v_admin is not null) or (v_player.id is not null and m.created_by_player_id = v_player.id and now() <= m.created_at + interval '5 minutes')),
            'can_delete', ((v_admin is not null) or (v_player.id is not null and m.created_by_player_id = v_player.id and now() <= m.created_at + interval '5 minutes'))
          ) as row_json
        from public.beerpong_matches m
        where m.deleted_at is null
          and ((v_admin is not null) or (v_player.id is not null and m.created_by_player_id = v_player.id))
        order by m.created_at desc
        limit v_limit
      ) s;
  elsif v_game = 'boerenbridge' then
    select coalesce(jsonb_agg(row_json order by created_at desc), '[]'::jsonb)
      into v_rows
      from (
        select
          m.created_at,
          jsonb_build_object(
            'client_match_id', coalesce(nullif(trim(coalesce(m.client_match_id,'')), ''), '__legacy__' || m.id::text),
            'raw_client_match_id', nullif(trim(coalesce(m.client_match_id,'')), ''),
            'internal_id', m.id,
            'created_at', m.created_at,
            'updated_at', m.updated_at,
            'finished_at', m.finished_at,
            'payload', coalesce(m.payload, '{}'::jsonb),
            'participant_names', coalesce((
              select jsonb_agg(coalesce(p->>'name', p->>'display_name', p->>'player_name', p::text) order by ord)
                from jsonb_array_elements(coalesce(m.payload->'players', m.payload->'participants', m.payload#>'{raw_payload,players}', '[]'::jsonb)) with ordinality as t(p, ord)
            ), '[]'::jsonb),
            'winner_names', coalesce(m.payload#>'{match_summary,winner_names}', m.payload->'winner_names', '[]'::jsonb),
            'can_edit', ((v_admin is not null) or (v_player.id is not null and m.created_by_player_id = v_player.id and now() <= m.created_at + interval '5 minutes')),
            'can_delete', ((v_admin is not null) or (v_player.id is not null and m.created_by_player_id = v_player.id and now() <= m.created_at + interval '5 minutes'))
          ) as row_json
        from public.boerenbridge_matches m
        where m.deleted_at is null
          and ((v_admin is not null) or (v_player.id is not null and m.created_by_player_id = v_player.id))
        order by m.created_at desc
        limit v_limit
      ) s;
  elsif v_game = 'klaverjas' then
    select coalesce(jsonb_agg(row_json order by created_at desc), '[]'::jsonb)
      into v_rows
      from (
        select
          j.created_at,
          jsonb_build_object(
            'client_match_id', j.id::text,
            'raw_client_match_id', j.id::text,
            'internal_id', j.id,
            'created_at', j.created_at,
            'updated_at', j.updated_at,
            'finished_at', j.created_at,
            'payload', coalesce(j.payload, '{}'::jsonb),
            'participant_names', coalesce((select jsonb_agg(e.display_name order by e.seat_no) from public.jas_game_entries e where e.game_id = j.id), '[]'::jsonb),
            'winner_names', coalesce((select jsonb_agg(e.display_name order by e.seat_no) from public.jas_game_entries e where e.game_id = j.id and coalesce(e.is_winner, false) = true), '[]'::jsonb),
            'can_edit', ((v_admin is not null) or (v_player.id is not null and j.created_by_player_id = v_player.id and now() <= j.created_at + interval '5 minutes')),
            'can_delete', ((v_admin is not null) or (v_player.id is not null and j.created_by_player_id = v_player.id and now() <= j.created_at + interval '5 minutes'))
          ) as row_json
        from public.jas_games j
        where j.deleted_at is null
          and ((v_admin is not null) or (v_player.id is not null and j.created_by_player_id = v_player.id))
        order by j.created_at desc
        limit v_limit
      ) s;
  else
    raise exception 'Onbekend speltype: %', v_game;
  end if;

  return jsonb_build_object(v_game, v_rows);
end;
$fn$;

create or replace function public.player_update_match_action(
  game_type_input text,
  session_token text,
  match_ref text,
  match_payload jsonb default '{}'::jsonb,
  delete_match boolean default false,
  reason_input text default null
) returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_game text := lower(trim(coalesce(game_type_input,'')));
  v_player public.players%rowtype;
  before_payload jsonb;
  v_name text;
  participant jsonb;
  resolved_player_id bigint;
  v_allowed boolean := false;
  v_ref jsonb;
  v_internal_id bigint;
  v_match_ref text;
begin
  select * into v_player from public._gejast_player_from_session(session_token);
  v_name := v_player.display_name;

  if v_game in ('beerpong','boerenbridge') then
    v_ref := public._resolve_match_control_ref(v_game, match_ref);
    v_internal_id := (v_ref->>'internal_id')::bigint;
    v_match_ref := coalesce(v_ref->>'match_ref', match_ref);
  else
    v_internal_id := null;
    v_match_ref := match_ref;
  end if;

  if v_game = 'beerpong' then
    select (created_by_player_id = v_player.id and now() <= created_at + interval '5 minutes') into v_allowed from public.beerpong_matches where id = v_internal_id;
    if not coalesce(v_allowed,false) then raise exception 'Bewerkenstijd verlopen.'; end if;
    select payload into before_payload from public.beerpong_matches where id = v_internal_id;
    if delete_match then
      update public.beerpong_matches set deleted_at = now(), updated_at = now() where id = v_internal_id;
    else
      update public.beerpong_matches
         set winner_team = coalesce(nullif(match_payload->>'winner_team',''), winner_team),
             team_a_player_names = coalesce((select array_agg(value::text) from jsonb_array_elements_text(coalesce(match_payload->'team_a_player_names', to_jsonb(team_a_player_names)))), team_a_player_names),
             team_b_player_names = coalesce((select array_agg(value::text) from jsonb_array_elements_text(coalesce(match_payload->'team_b_player_names', to_jsonb(team_b_player_names)))), team_b_player_names),
             team_a_cups_left = coalesce(nullif(match_payload->>'team_a_cups_left','')::integer, team_a_cups_left),
             team_b_cups_left = coalesce(nullif(match_payload->>'team_b_cups_left','')::integer, team_b_cups_left),
             finished_at = coalesce(nullif(match_payload->>'finished_at','')::timestamptz, finished_at),
             payload = match_payload,
             deleted_at = null,
             updated_at = now()
       where id = v_internal_id;
    end if;
  elsif v_game = 'boerenbridge' then
    select (created_by_player_id = v_player.id and now() <= created_at + interval '5 minutes') into v_allowed from public.boerenbridge_matches where id = v_internal_id;
    if not coalesce(v_allowed,false) then raise exception 'Bewerkenstijd verlopen.'; end if;
    select payload into before_payload from public.boerenbridge_matches where id = v_internal_id;
    if delete_match then
      update public.boerenbridge_matches set deleted_at = now(), updated_at = now() where id = v_internal_id;
    else
      update public.boerenbridge_matches
         set payload = match_payload,
             finished_at = coalesce(nullif(match_payload->>'finished_at','')::timestamptz, finished_at),
             match_status = case when nullif(match_payload->>'finished_at','') is not null then 'finished' else coalesce(match_status,'draft') end,
             deleted_at = null,
             updated_at = now()
       where id = v_internal_id;
    end if;
  elsif v_game = 'klaverjas' then
    select (created_by_player_id = v_player.id and now() <= created_at + interval '5 minutes') into v_allowed from public.jas_games where id = match_ref::bigint;
    if not coalesce(v_allowed,false) then raise exception 'Bewerkenstijd verlopen.'; end if;
    select payload into before_payload from public.jas_games where id = match_ref::bigint;
    if delete_match then
      update public.jas_games set deleted_at = now(), updated_at = now() where id = match_ref::bigint;
    else
      update public.jas_games
         set title = coalesce(nullif(trim(coalesce(match_payload->>'title','')), ''), title),
             played_at = coalesce(nullif(match_payload->>'played_at','')::date, played_at),
             payload = match_payload,
             deleted_at = null,
             updated_at = now()
       where id = match_ref::bigint;
      delete from public.jas_game_entries where game_id = match_ref::bigint;
      for participant in select * from jsonb_array_elements(coalesce(match_payload->'participants','[]'::jsonb)) loop
        select p.id into resolved_player_id from public.players p where lower(p.display_name) = lower(trim(participant ->> 'name')) limit 1;
        insert into public.jas_game_entries(game_id, player_id, display_name, seat_no, team_no, total_points, is_winner)
        values (match_ref::bigint, resolved_player_id, participant->>'name', (participant->>'seat_no')::integer, (participant->>'team_no')::integer, coalesce((participant->>'total_points')::integer,0), coalesce((participant->>'is_winner')::boolean,false));
      end loop;
    end if;
  else
    raise exception 'Onbekend speltype';
  end if;

  insert into public.match_change_log(game_type, game_key, match_ref, client_match_id, change_type, changed_by_role, changed_by_name, reason, before_payload, after_payload)
  values (v_game, v_game, coalesce(v_match_ref, match_ref), coalesce(v_match_ref, match_ref), case when delete_match then 'delete' else 'update' end, 'player', v_name, reason_input, before_payload, match_payload);
  perform public._enqueue_rating_rebuild(v_game, case when delete_match then 'player_delete' else 'player_update' end, coalesce(v_match_ref, match_ref), 'player', v_name);
  perform public.process_game_rating_rebuild_queue(10);
  return jsonb_build_object('ok', true, 'match_ref', coalesce(v_match_ref, match_ref));
end;
$fn$;

