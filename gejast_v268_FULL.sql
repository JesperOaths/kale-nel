create or replace function public.admin_set_player_ghost_status(
  admin_session_token text,
  target_player_id bigint default null,
  target_display_name text default null,
  ghosted boolean default true,
  reason_input text default null
) returns jsonb
language plpgsql
security definer
set search_path to 'public'
as $fn$
declare
  v_admin jsonb;
  v_admin_name text;
  v_player public.players%rowtype;
  v_target_name text := nullif(trim(coalesce(target_display_name,'')), '');
  v_original_name text;
  v_alias text;
  v_reason text := nullif(trim(coalesce(reason_input,'')), '');
  v_hidden_name text;
  v_has_updated boolean := false;
begin
  v_admin := to_jsonb(public.admin_check_session(admin_session_token));
  if coalesce((v_admin->>'ok')::boolean, true) is not true then
    raise exception 'Log opnieuw in als admin.';
  end if;
  v_admin_name := coalesce(nullif(trim(v_admin->>'admin_username'),''), nullif(trim(v_admin->>'username'),''), 'admin');

  if target_player_id is not null then
    select * into v_player from public.players where id = target_player_id;
  elsif v_target_name is not null then
    select p.* into v_player
    from public.players p
    where lower(trim(coalesce(p.display_name,''))) = lower(v_target_name)
       or lower(trim(coalesce(to_jsonb(p)->>'username',''))) = lower(public._normalize_allowed_username(v_target_name))
       or exists (select 1 from jsonb_array_elements_text(case when jsonb_typeof(coalesce(to_jsonb(p)->'ghost_original_names','[]'::jsonb))='array' then coalesce(to_jsonb(p)->'ghost_original_names','[]'::jsonb) else '[]'::jsonb end) g(value) where lower(trim(g.value)) = lower(v_target_name))
    order by p.id desc
    limit 1;
  end if;

  if ghosted then
    if v_player.id is not null then
      v_original_name := coalesce(v_target_name, nullif(trim(coalesce(v_player.display_name,'')), ''));
      perform public._mark_player_hidden(v_player.id, true, true, v_reason);
      if v_original_name is not null then
        update public.players
           set ghost_original_names = (
             select coalesce(jsonb_agg(distinct entry), '[]'::jsonb)
             from (
               select value::text as entry from jsonb_array_elements_text(case when jsonb_typeof(coalesce(public.players.ghost_original_names,'[]'::jsonb))='array' then coalesce(public.players.ghost_original_names,'[]'::jsonb) else '[]'::jsonb end)
               union all select v_original_name
             ) s where nullif(trim(coalesce(entry,'')), '') is not null
           )
         where id = v_player.id;
      end if;
      select exists(select 1 from information_schema.columns where table_schema='public' and table_name='players' and column_name='updated_at') into v_has_updated;
      v_alias := '__ghost_' || v_player.id::text;
      execute 'update public.players set display_name = $2' || case when v_has_updated then ', updated_at = now()' else '' end || ' where id = $1' using v_player.id, v_alias;
      if to_regclass('public.sessions') is not null then
        begin execute 'delete from public.sessions where player_id = $1' using v_player.id; exception when others then null; end;
      end if;
      if v_original_name is not null then
        insert into public.hidden_site_names(name_norm, display_name, hidden_reason, hidden_at, hidden_by, released_at)
        values (lower(v_original_name), v_original_name, coalesce(v_reason, 'Verborgen via admin'), now(), v_admin_name, null)
        on conflict (name_norm) do update set display_name=excluded.display_name, hidden_reason=excluded.hidden_reason, hidden_at=excluded.hidden_at, hidden_by=excluded.hidden_by, released_at=null;
        insert into public.allowed_usernames(username, display_name, status, reserved_for_email, reserved_for_person_note, player_id, current_claim_request_id, activation_link_id, blocked_reason, updated_at)
        values (public._normalize_allowed_username(v_original_name), v_original_name, 'available', null, null, null, null, null, null, now())
        on conflict do nothing;
        update public.allowed_usernames set username=public._normalize_allowed_username(v_original_name), status='available', player_id=null, current_claim_request_id=null, activation_link_id=null, blocked_reason=null, updated_at=now() where lower(trim(coalesce(display_name,''))) = lower(v_original_name);
      end if;
      for v_hidden_name in select value from jsonb_array_elements_text(case when jsonb_typeof(coalesce((select ghost_original_names from public.players where id=v_player.id),'[]'::jsonb))='array' then coalesce((select ghost_original_names from public.players where id=v_player.id),'[]'::jsonb) else '[]'::jsonb end)
      loop
        if nullif(trim(v_hidden_name), '') is not null then
          insert into public.hidden_site_names(name_norm, display_name, hidden_reason, hidden_at, hidden_by, released_at)
          values (lower(trim(v_hidden_name)), trim(v_hidden_name), coalesce(v_reason, 'Verborgen via admin'), now(), v_admin_name, null)
          on conflict (name_norm) do update set display_name=excluded.display_name, hidden_reason=excluded.hidden_reason, hidden_at=excluded.hidden_at, hidden_by=excluded.hidden_by, released_at=null;
        end if;
      end loop;
      return jsonb_build_object('ok', true, 'player_id', v_player.id, 'display_name', v_original_name, 'ghosted', true, 'mode', 'player');
    elsif v_target_name is not null then
      insert into public.hidden_site_names(name_norm, display_name, hidden_reason, hidden_at, hidden_by, released_at)
      values (lower(v_target_name), v_target_name, coalesce(v_reason, 'Verborgen via admin'), now(), v_admin_name, null)
      on conflict (name_norm) do update set display_name=excluded.display_name, hidden_reason=excluded.hidden_reason, hidden_at=excluded.hidden_at, hidden_by=excluded.hidden_by, released_at=null;
      insert into public.allowed_usernames(username, display_name, status, reserved_for_email, reserved_for_person_note, player_id, current_claim_request_id, activation_link_id, blocked_reason, updated_at)
      values (public._normalize_allowed_username(v_target_name), v_target_name, 'available', null, null, null, null, null, null, now())
      on conflict do nothing;
      update public.allowed_usernames set username=public._normalize_allowed_username(v_target_name), status='available', player_id=null, current_claim_request_id=null, activation_link_id=null, blocked_reason=null, updated_at=now() where lower(trim(coalesce(display_name,''))) = lower(v_target_name);
      return jsonb_build_object('ok', true, 'display_name', v_target_name, 'ghosted', true, 'mode', 'name_only');
    else
      raise exception 'Geef een target_player_id of target_display_name mee.';
    end if;
  else
    if v_target_name is not null then
      update public.hidden_site_names set released_at = now() where name_norm = lower(v_target_name) and released_at is null;
    end if;
    if v_player.id is not null then
      perform public._mark_player_hidden(v_player.id, false, coalesce(v_player.is_dummy, false), v_reason);
    end if;
    return jsonb_build_object('ok', true, 'display_name', coalesce(v_target_name, v_player.display_name), 'ghosted', false);
  end if;
end;
