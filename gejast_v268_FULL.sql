create or replace function public._insert_match_change_log(
  p_game_type text,
  p_match_ref text,
  p_change_type text,
  p_actor_role text,
  p_actor_name text,
  p_reason text default null,
  p_before_payload jsonb default null,
  p_after_payload jsonb default null,
  p_client_match_id text default null
) returns void
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  rec record;
  cols text[] := array[]::text[];
  vals text[] := array[]::text[];
  v_game text := lower(trim(coalesce(nullif(p_game_type,''), 'unknown')));
  v_match_ref text := coalesce(nullif(trim(coalesce(p_match_ref,'')), ''), nullif(trim(coalesce(p_client_match_id,'')), ''), 'unknown');
  v_client_match_id text := coalesce(nullif(trim(coalesce(p_client_match_id,'')), ''), v_match_ref, 'unknown');
  v_change text := lower(trim(coalesce(nullif(p_change_type,''), 'update')));
  v_role text := lower(trim(coalesce(nullif(p_actor_role,''), 'system')));
  v_name text := coalesce(nullif(trim(coalesce(p_actor_name,'')), ''), v_role, 'system');
  v_before jsonb := coalesce(p_before_payload, '{}'::jsonb);
  v_after jsonb := coalesce(p_after_payload, '{}'::jsonb);
  v_sql text;
  v_value_sql text;
begin
  for rec in
    select column_name, data_type, udt_name, is_nullable, column_default
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'match_change_log'
      and column_name <> 'id'
    order by ordinal_position
  loop
    v_value_sql := null;

    case rec.column_name
      when 'game_type', 'game_key' then v_value_sql := format('%L', v_game);
      when 'match_ref' then v_value_sql := format('%L', v_match_ref);
      when 'client_match_id' then v_value_sql := format('%L', v_client_match_id);
      when 'change_type', 'action' then v_value_sql := format('%L', v_change);
      when 'changed_by_role', 'actor_role', 'actor_type' then v_value_sql := format('%L', v_role);
      when 'changed_by_name', 'actor_name' then v_value_sql := format('%L', v_name);
      when 'reason' then v_value_sql := case when nullif(trim(coalesce(p_reason,'')), '') is null then 'null' else format('%L', trim(p_reason)) end;
      when 'before_payload', 'payload_before' then v_value_sql := format('%L::jsonb', v_before::text);
      when 'after_payload', 'payload_after' then v_value_sql := format('%L::jsonb', v_after::text);
      when 'changed_at' then v_value_sql := 'now()';
      else v_value_sql := null;
    end case;

    if v_value_sql is null then
      if rec.column_default is not null then
        continue;
      elsif rec.is_nullable = 'YES' then
        continue;
      elsif rec.udt_name in ('json','jsonb') then
        v_value_sql := '''{}''::jsonb';
      elsif rec.data_type like 'timestamp%' then
        v_value_sql := 'now()';
      elsif rec.data_type = 'date' then
        v_value_sql := 'current_date';
      elsif rec.data_type in ('text','character varying','character') then
        v_value_sql := format('%L', 'auto');
      elsif rec.data_type = 'boolean' then
        v_value_sql := 'false';
      elsif rec.data_type in ('smallint','integer','bigint','numeric','real','double precision') then
        v_value_sql := '0';
      else
        continue;
      end if;
    end if;

    cols := array_append(cols, format('%I', rec.column_name));
    vals := array_append(vals, v_value_sql);
  end loop;

  if array_length(cols, 1) is null then
    return;
  end if;

  v_sql := 'insert into public.match_change_log(' || array_to_string(cols, ', ') || ') values (' || array_to_string(vals, ', ') || ')';
  execute v_sql;
end;
$$;

