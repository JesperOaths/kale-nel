create or replace function public._is_public_site_name(p_name text)
returns boolean
language plpgsql
security definer
set search_path to 'public'
as $$
declare
  v_name text := trim(coalesce(p_name,''));
  v_norm text := lower(v_name);
  v_has_player boolean := false;
  v_has_visible boolean := false;
  v_hidden boolean := false;
begin
  if v_norm = '' then
    return false;
  end if;

  if to_regclass('public.hidden_site_names') is not null then
    select exists(
      select 1 from public.hidden_site_names h
      where h.name_norm = v_norm and h.released_at is null
    ) into v_hidden;
    if v_hidden then
      return false;
    end if;
  end if;

  select exists(
    select 1
    from public.players p
    where lower(trim(coalesce(p.display_name,''))) = v_norm
       or lower(trim(coalesce(to_jsonb(p)->>'username',''))) = v_norm
       or exists (
         select 1
         from jsonb_array_elements_text(
           case when jsonb_typeof(coalesce(to_jsonb(p)->'ghost_original_names','[]'::jsonb)) = 'array'
                then coalesce(to_jsonb(p)->'ghost_original_names','[]'::jsonb)
                else '[]'::jsonb end
         ) g(value)
         where lower(trim(g.value)) = v_norm
       )
  ) into v_has_player;

  select exists(
    select 1
    from public.players p
    where (
       lower(trim(coalesce(p.display_name,''))) = v_norm
       or lower(trim(coalesce(to_jsonb(p)->>'username',''))) = v_norm
       or exists (
         select 1
         from jsonb_array_elements_text(
           case when jsonb_typeof(coalesce(to_jsonb(p)->'ghost_original_names','[]'::jsonb)) = 'array'
                then coalesce(to_jsonb(p)->'ghost_original_names','[]'::jsonb)
                else '[]'::jsonb end
         ) g(value)
         where lower(trim(g.value)) = v_norm
       )
    )
    and coalesce((to_jsonb(p)->>'hidden_from_public')::boolean, false) is false
    and coalesce((to_jsonb(p)->>'is_dummy')::boolean, false) is false
    and nullif(coalesce(to_jsonb(p)->>'ghosted_at',''), '') is null
  ) into v_has_visible;

  if v_has_player then
    return v_has_visible;
  end if;

  if v_norm ~ '(dummy|__ghost|ghost|testuser|verifier\s*1111|verifier|spookinoza)' then
    return false;
  end if;

  return true;
end;
$$;

